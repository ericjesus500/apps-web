            <h2 id="comofunc" class=" text-lila mt-5"><em>Cómo Funciona Express.js</em></h2>
            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">Dado que Express.js utiliza el modelo cliente-servidor para aceptar las peticiones de los usuarios y devolver las respuestas al cliente, su funcionamiento no es muy diferente del de otros frameworks populares, como Laravel.</p>
            </div>

            <p class="pathway-extreme-p text-justify mt-4">Cuando un usuario envía una petición desde su navegador web escribiendo la dirección de un sitio web, el navegador envía una petición HTTP a la aplicación/servidor (muchas aplicaciones creadas con Express.js están alojadas en algún lugar de la nube).</p>
            <p class="pathway-extreme-p text-justify mt-4">El servidor recibirá la petición a través de una de sus rutas y la procesará utilizando el controlador que coincida con la ruta solicitada.</p>
            <p class="pathway-extreme-p text-justify mt-4">Tras el procesamiento, el servidor enviará una respuesta al cliente utilizando HTTP, ya que se trata de un protocolo de comunicación de ida y vuelta. La respuesta devuelta al cliente puede ser un texto estándar, una página HTML dinámica que el navegador procesará y mostrará una bonita página web, o datos JSON que los desarrolladores del frontend manejarán para mostrar información en la página web.</p>
            <p class="pathway-extreme-p text-justify mt-4">Vamos a crear un servidor sencillo que escuche las peticiones entrantes desde una URL y un número de puerto específicos con Express.js:</p>
            <pre class="sintaxis">
              <code>
                  const express = require('express')
                  const app = express()
                  const port = 4000

                  app.get('/', (request, response) => {
                  &nbsp;&nbsp;&nbsp;response.send('Testing Hello World!')
                  })

                  app.listen(port, () => {
                      &nbsp;&nbsp;&nbsp;console.log(`Test app listening at http://localhost:${port}`)
                  })
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Este es un simple servidor Express.js que escuchará las peticiones entrantes en http://localhost:4000/ y devolverá una respuesta de texto de «¡Probando Hola Mundo!«.</p>
          </article>

