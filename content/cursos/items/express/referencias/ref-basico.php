            <h2 id="concepto" class="text-lila mt-5"><em>Qué es express</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">Express.js es el framework backend más popular para Node.js, y es una parte extensa del ecosistema JavaScript.</p>
            </div>

            <p class="pathway-extreme-p text-justify mt-4">Está diseñado para construir aplicaciones web de una sola página, multipágina e híbridas, también se ha convertido en el estándar para desarrollar aplicaciones backend con Node.js, y es la parte backend de algo conocido como la pila MEVN.</p>
            <p class="pathway-extreme-p text-justify mt-4">El MEVN es un stack de software JavaScript libre y de código abierto para construir sitios y aplicaciones web dinámicas que tiene los siguientes componentes</p>
            <ul>
              <li class="my-2"><i class="bi bi-check2-square px-2 text-orange"></i>MongoDB: MongoDB es la base de datos NoSQL estándar</li>
              <li class="my-2"><i class="bi bi-check2-square px-2 text-orange"></i>Express.js: El framework de aplicaciones web por defecto para construir aplicaciones web</li>
              <li class="my-2"><i class="bi bi-check2-square px-2 text-orange"></i>Vue.js: El framework progresivo de JavaScript utilizado para construir aplicaciones web front-end</li>
              <li class="my-2"><i class="bi bi-check2-square px-2 text-orange"></i>Node.js: El motor de JavaScript utilizado para aplicaciones escalables del lado del servidor y de la red.</li>
            </ul>
            <h3 class="mt-5">Qué es express.js</h3>
            <p class="pathway-extreme-p text-justify mt-4">Express.js, a veces también llamado «Express», es un framework de backend Node.js minimalista, rápido y similar a Sinatra, que proporciona características y herramientas robustas para desarrollar aplicaciones de backend escalables. Te ofrece el sistema de enrutamiento y características simplificadas para ampliar el framework con componentes y partes más potentes en función de los casos de uso de tu aplicación.</p>
            <p class="pathway-extreme-p text-justify mt-4">El framework proporciona un conjunto de herramientas para aplicaciones web, peticiones y respuestas HTTP, enrutamiento y middleware para construir y desplegar aplicaciones a gran escala y preparadas para la empresa.</p>
            <p class="pathway-extreme-p text-justify mt-4">También proporciona una herramienta de interfaz de línea de comandos (CLI) llamada Node Package Manager (NPM), donde los desarrolladores pueden obtener paquetes desarrollados. También obliga a los desarrolladores a seguir el principio de No te repitas (DRY).</p>
            <p class="pathway-extreme-p text-justify mt-4">El principio DRY pretende reducir la repetición de patrones de software, sustituyéndolos por abstracciones, o utilizando normalizaciones de datos para evitar la redundancia.</p>

