          <article class="w-100 px-4 my-5 article">
            <h2 id="instalacion" class="text-lila mt-5"><em>Instalando Express</em></h2>

            <p class="pathway-extreme-p text-justify mt-4">Suponiendo que ya ha instalado Node.js, cree un directorio para contener su aplicación y conviértalo en su directorio de trabajo.</p>
            <pre class="sintaxis">
              <code>
                $ mkdir myapp
                $ cd myapp
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Utilice el comando npm init para crear un archivo package.json para su aplicación.</p>
            <pre class="sintaxis">
              <code>
                $ npm init
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Este comando le solicita varias cosas, como el nombre y la versión de su aplicación. Por ahora, puedes simplemente presionar RETORNO para aceptar los valores predeterminados para la mayoría de ellos, con la siguiente excepción:</p>
            <pre class="sintaxis">
              <code>
                entry point: (index.js)
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Ingrese app.js, o el nombre que desee que tenga el archivo principal. Si desea que sea index.js, presione RETORNO para aceptar el nombre de archivo predeterminado sugerido.</p>
            <p class="pathway-extreme-p text-justify mt-4">Ahora instale Express en el directorio myapp y guárdelo en la lista de dependencias. Por ejemplo:</p>
            <pre class="sintaxis">
              <code>
                $ npm install express
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Para instalar Express temporalmente y no agregarlo a la lista de dependencias:</p>
            <pre class="sintaxis">
              <code>
                $ npm install express --no-save
              </code>
            </pre>
            <h3 class="mt-5">Hello world example</h3>
            <pre class="sintaxis">
              <code>
                const express = require('express')
                const <span class="text-primary">app</span> = express()
                const port = 3000

                <span class="text-primary">app</span>.<span class="span text-danger">get</span>('/', (req, res) => {
                  &nbsp;&nbsp;&nbsp;res.send('Hello World!')
                })

                <span class="text-primary">app</span>.<span class="span text-danger">listen</span>(port, () => {
                  &nbsp;&nbsp;&nbsp;console.log(`Aplicación de ejemplo escuchando en el puerto ${port}`)
                })
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Esta aplicación inicia un servidor y escucha las conexiones en el puerto 3000. La aplicación responde con "¡Hola mundo!" para solicitudes a la URL raíz (/) o ruta. Para cualquier otra ruta, responderá con un 404 No encontrado.</p>

