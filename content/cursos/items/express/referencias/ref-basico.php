          <!-- Routing Básico -->
          <article class="px-4">
            <h2 id="routing" class=" text-lila mt-5"><em>Routing Básico</em></h2>
            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">El enrutamiento se refiere a determinar cómo responde una aplicación a una solicitud de cliente a un punto final particular, que es un URI (o ruta) y un método de solicitud HTTP específico (GET, POST, etc.).</p>
            </div>

            <p class="pathway-extreme-p text-justify mt-4">Cada ruta puede tener una o más funciones de controlador, que se ejecutan cuando la ruta coincide.</p>
            <p class="pathway-extreme-p text-justify mt-4">La definición de ruta toma la siguiente estructura:</p>
            <pre class="sintaxis">
              <code>
                <span class="text-primary">app</span>.<span class="text-danger">METHOD</span>(PATH, HANDLER)
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Donde:</p>
            <ul>
              <li class="my-2"><i class="bi bi-check2-square px-2 text-orange"></i>app es una instancia de express.</li>
              <li class="my-2"><i class="bi bi-check2-square px-2 text-orange"></i>METHOD es un método de solicitud HTTP, en minúsculas.</li>
              <li class="my-2"><i class="bi bi-check2-square px-2 text-orange"></i>PATH es una ruta en el servidor.</li>
              <li class="my-2"><i class="bi bi-check2-square px-2 text-orange"></i>HANDLER es la función que se ejecuta cuando la ruta coincide.</li>
            </ul>
            <p class="pathway-extreme-p text-justify mt-4">Los siguientes ejemplos ilustran la definición de rutas simples.</p>
            <p class="pathway-extreme-p text-justify mt-4">Responde con ¡Hola mundo! en la página de inicio:</p>
            <pre class="sintaxis">
              <code>
                <span class="text-primary">app</span>.<span class="text-danger">get</span>('/', (req, res)=>{
                  &nbsp;&nbsp;&nbsp;<span class="text-indigo">res</span>.<span class="text-lila">send</span>('Hello World!')
                })
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Responda a la solicitud POST en la ruta raíz (/), la página de inicio de la aplicación:</p>
            <pre class="sintaxis">
              <code>
                <span class="text-primary">app</span>.<span class="text-danger">post</span>('/', (req, res)=>{
                  &nbsp;&nbsp;&nbsp;<span class="text-indigo">res</span>.<span class="text-lila">send</span>('Recibí una solicitud POST')
                })
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Responder a una solicitud PUT a la ruta /user:</p>
            <pre class="sintaxis">
              <code>
                <span class="text-primary">app</span>.<span class="text-danger">put</span>('/user', (req, res)=>{
                  &nbsp;&nbsp;&nbsp;<span class="text-indigo">res</span>.<span class="text-lila">send</span>('Recibí una solicitud PUT en /user')
                })
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Responda a una solicitud DELETE a la ruta /user:</p>
            <pre class="sintaxis">
              <code>
                <span class="text-primary">app</span>.<span class="text-danger">delete</span>('/user', (req, res)=>{
                  &nbsp;&nbsp;&nbsp;<span class="text-indigo">res</span>.<span class="text-lila">send</span>('Recibí una solicitud DELETE en /user')
                })
              </code>
            </pre>
          </article>

          <!-- Sirviendo archivos estáticos en Express -->
          <article class="px-4">
            <h2 id="express_estatico" class=" text-lila mt-5"><em>Sirviendo archivos estáticos en Express</em></h2>
            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">Para servir archivos estáticos como imágenes, archivos CSS y archivos JavaScript, utilice la función de middleware integrada express.static en Express.</p>
            </div>

            <p class="pathway-extreme-p text-justify mt-4">La firma de la función es:</p>
            <pre class="sintaxis">
              <code>
                express.static(root, [options])
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">El argumento <strong>root</strong> especifica el directorio raíz desde el cual servir los activos estáticos. Para obtener más información sobre el argumento de opciones, consulte <a href="https://expressjs.com/en/4x/api.html#express.static" target="_blank" class="btn-link">express.static</a>.</p>
            <p class="pathway-extreme-p text-justify mt-4">Por ejemplo, utilice el siguiente código para publicar imágenes, archivos CSS y archivos JavaScript en un directorio denominado <strong>public</strong>:</p>
            <pre class="sintaxis">
              <code>
                app.use(express.static('public'))
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Ahora, puedes cargar los archivos que están en el directorio público:</p>
            <pre class="sintaxis">
              <code>
              http://localhost:3000/images/kitten.jpg
              http://localhost:3000/css/style.css
              http://localhost:3000/js/app.js
              http://localhost:3000/images/bg.png
              http://localhost:3000/hello.html
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Para utilizar varios directorios de activos estáticos, llame a la función de middleware express.static varias veces:</p>
            <pre class="sintaxis">
              <code>
                app.use(express.static('public'))
                app.use(express.static('files'))
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Express busca los archivos en el orden en que configuró los directorios estáticos con la función de middleware express.static.</p>
            <p class="pathway-extreme-p text-justify mt-4">Para crear un prefijo de ruta virtual (donde la ruta realmente no existe en el sistema de archivos) para los archivos servidos por la función express.static, especifique una ruta de montaje para el directorio estático, como se muestra a continuación:</p>
            <pre class="sintaxis">
              <code>
                app.use('/static', express.static('public'))
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Ahora, puede cargar los archivos que están en el directorio público desde el prefijo de ruta /static.</p>
            <pre class="sintaxis">
              <code>
                http://localhost:3000/static/images/kitten.jpg
                http://localhost:3000/static/css/style.css
                http://localhost:3000/static/js/app.js
                http://localhost:3000/static/images/bg.png
                http://localhost:3000/static/hello.html
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Sin embargo, la ruta que proporciona a la función express.static es relativa al directorio desde donde inicia su proceso de nodo. Si ejecuta la aplicación express desde otro directorio, es más seguro usar la ruta absoluta del directorio que desea servir:</p>
            <pre class="sintaxis">
              <code>
                const path = require('path')
                app.use('/static', express.static(path.join(__dirname, 'public')))
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Para obtener más detalles sobre la función de servicio estático y sus opciones, consulte <a href="https://expressjs.com/resources/middleware/serve-static.html" target="_blank" class="img-fluid">serve-static</a>.</p>
          </article>

