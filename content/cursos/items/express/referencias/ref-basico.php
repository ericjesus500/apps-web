            <h2 id="porque" class="text-lila mt-5"><em>Por Qué Deberías usar Express.js</em></h2>
            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">Ofrece una mayor velocidad de E/S para peticiones y respuestas más rápidas además de su sistema de hilo único y procesos asíncronos.</p>
            </div>

            <p class="text-justify mt-4">También utiliza la estructura MVC para simplificar las manipulaciones de datos y los sistemas de enrutamiento.</p>
            <p class="text-justify mt-4">Echemos un vistazo a algunas de las principales razones por las que deberías considerar el uso de Express.js.</p>
            <h3 class="mt-5">Flexible y Rápido</h3>
            <p class="text-justify mt-4">Express.js es muy fácil de usar y flexible, y es más rápido que cualquier otro framework Node.js. Al ser un framework minimalista, ofrece un rápido desarrollo de aplicaciones y alivia el estrés de dominar las diferentes partes de un framework más grande. También proporciona ricas características, como un excelente sistema de enrutamiento, middlewares y negociación de contenidos desde el primer momento.</p>
            <h3 class="mt-5">Parte de la Stack MEAN</h3>
            <p class="text-justify mt-4">Express.js es el framework de elección en cada stack representado con la E en cualquiera de los stacks, como MERN, MEAN, etc. También se puede integrar fácilmente en cualquier stack o tecnología para mostrar lo vital que es el framework en el proceso de desarrollo del stack MEAN.</p>
            <p class="text-justify mt-4">Además, puede conectarse eficazmente con un sistema de gestión de bases de datos más robusto que el convencional MySQL y proporciona un proceso de desarrollo sin fisuras en cada stack. Esta combinación de características hace que Express.js sea muy popular entre los desarrolladores MEAN.</p>
            <h3 class="mt-5">Escalabilidad</h3>
            <p class="text-justify mt-4">Express.js ha demostrado ser muy escalable a lo largo de los años debido al número de grandes empresas que utilizan el framework en su servidor diariamente.</p>
            <p class="text-justify mt-4">Maneja las peticiones y respuestas de los usuarios de forma eficiente y no requiere apenas configuración adicional cuando se desarrolla una aplicación web a gran escala.</p>
            <p class="text-justify mt-4">Tiene excelentes módulos, paquetes y recursos adicionales, que ayudan a los desarrolladores a crear aplicaciones web fiables y escalables.</p>
            <h3 class="mt-5">Compatible con el Motor V8 de Google</h3>
            <p class="text-justify mt-4">Express.js es compatible con muchos paquetes del motor V8 de Google, lo que hace que el framework sea muy potente para construir y desplegar aplicaciones en tiempo real, colaborativas y basadas en la red a nivel empresarial.</p>
            <p class="text-justify mt-4">El motor V8 de Google es un motor JavaScript y WebAssembly de código abierto de alto rendimiento que admite una gran velocidad y escalabilidad para aplicaciones complejas e intensas. Cuando utilizas paquetes que emplean el motor Google V8, tu aplicación backend recibe un enorme impulso de rendimiento y escalabilidad.</p>
            <h3 class="mt-5">Apoyo de la Comunidad</h3>
            <p class="text-justify mt-4">Como el framework es el más popular de Node.js backend, tiene el mayor número de apoyo de la comunidad, recursos y paquetes para cualquier desafío de desarrollo. El apoyo de Google también es amplio, por lo que el framework es una opción popular entre los desarrolladores de Node.js. Su naturaleza de código abierto da a los desarrolladores la oportunidad de crear paquetes y recursos extensibles para facilitar el desarrollo, no sólo para ellos mismos, sino para todos los que programan con Express.js.</p>
            <h3 class="mt-5">Potente Sistema de Enrutamiento</h3>
            <p class="text-justify mt-4">El framework tiene el sistema de enrutamiento más potente y robusto incorporado por defecto que asiste a su aplicación en respuesta a una solicitud del cliente a través de un endpoint particular.</p>
            <p class="text-justify mt-4">Con el sistema de enrutamiento de Express.js, puedes dividir tu abultado sistema de enrutamiento en archivos manejables utilizando la instancia de enrutamiento del framework.</p>
            <p class="text-justify mt-4">El sistema de enrutamiento de Express es útil para gestionar la estructura de tu aplicación, agrupando diferentes rutas en una única carpeta/directorio.</p>
            <p class="text-justify mt-4">Los desarrolladores crean códigos más mantenibles agrupando las funcionalidades con el enrutador Express y evitando la repetición.</p>
            <h3 class="mt-5">Middleware</h3>
            <p class="text-justify mt-4">Express.js es un framework que comprende una serie de middleware para crear un proceso de desarrollo sin fisuras.</p>
            <p class="text-justify mt-4">Los middlewares son códigos que se ejecutan antes de que una petición HTTP llegue al manejador de rutas o antes de que un cliente reciba una respuesta, lo que da al framework la capacidad de ejecutar un script típico antes o después de la petición de un cliente.</p>
            <p class="text-justify mt-4">Con los middlewares, los desarrolladores pueden introducir scripts para interceptar el flujo de la aplicación; por ejemplo, los desarrolladores pueden utilizar los middlewares para comprobar si un usuario se ha conectado o desconectado correctamente.</p>
