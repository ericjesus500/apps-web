          <h1 class="my-5"><span class="bg-success px-3 rounded">Middleware</span></h1>

          <article class="px-4 my-5 article">

            <img src="../src/img/middleware.jpg" alt="middleware" class="img-fluid mt-5">

            <h3 id="concepto" class="text-primary my-5">¿Qué es el Middleware?</h3>
            <div class="callout callout-success mt-5">
              <p class="text-success">Un middleware es un software con función de conector que vincula diferentes sistemas, programas y/o aplicaciones entre sí, a menudo complejos y ya existentes, que no fueron diseñados originalmente para estar conectados. Actúa como puente de integración entre varias tecnologías, aplicaciones, herramientas y otras bases de datos diversas.</p>
            </div>
            <p class="my-5">Además, proporciona una capa de conectividad para sensores y también para las capas de aplicación que brindan servicios que aseguran comunicaciones efectivas entre un software.</p>

            <h3 id="importancia" class="text-primary my-5">¿Por qué es importante el middleware?</h3>
            <p class="my-5">El middleware comenzó como un puente entre aplicaciones nuevas y sistemas heredados. Los desarrolladores lo utilizaron para integrar nuevos programas con sistemas anteriores sin reescribir el código previo</p>
            <p class="my-5">Los desarrolladores utilizan el middleware para respaldar el desarrollo de las aplicaciones y simplificar los procesos de diseño. Esto, les permite dedicar más tiempo a la lógica de negocio y a las características en lugar de a la conectividad entre los diferentes componentes del software. Sin la ayuda del middleware, los desarrolladores tendrían que crear un módulo de intercambio de datos para cada componente de software que se conecta con la aplicación. Esto representa un desafío muy grande, ya que las aplicaciones modernas consisten en múltiples microservicios o componentes de software pequeños que interactúan.</p>

            <h3 id="casos" class="text-primary my-5 ">
              ¿Cuáles son los casos de uso del middleware?
            </h3>
            <h4 class="mt-5 mb-2 text-indigo"><strong>Desarrollo de videojuegos</strong></h4>
            <p class="my-2">Los desarrolladores de videojuegos utilizan el middleware como motor de videojuegos. Para que un videojuego funcione, el software debe comunicarse con varios servidores de imagen, audio y video en conjunto con los sistemas de comunicación. El motor de videojuegos facilita esta comunicación y hace que el desarrollo de videojuegos sea mucho más eficiente.</p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Electrónica</strong></h4>
            <p class="my-2">Los ingenieros electrónicos utilizan el middleware para integrar diversos tipos de sensores en sus controladores. La capa del middleware permite que los sensores se comuniquen con el controlador por medio de un marco de mensajería en común.</p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Desarrollo de software</strong></h4>
            <p class="my-2">Los desarrolladores de software utilizan el middleware para integrar diferentes componentes de software a otras aplicaciones. El middleware ofrece una interfaz de programación de aplicaciones (API) estándar para administrar la entrada y la salida de los datos requeridos desde el componente. El enlace interno con el componente se encuentra oculto para el usuario. Los desarrolladores utilizan las API para solicitar los servicios que necesitan de los componentes de software.</p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Transmisión de datos</strong></h4>
            <p class="my-2">Las aplicaciones de software utilizan el middleware para enviar y recibir secuencias de datos de manera fiable. Las secuencias de datos son una transmisión de alta velocidad de datos continuos. Estos son importantes para la transmisión de audio y video fiables.</p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Aplicaciones distribuidas</strong></h4>
            <p class="my-2">Las aplicaciones distribuidas son programas de software que se ejecutan en diferentes computadoras dentro de una red. A menudo, consisten en aplicaciones frontend y backend. Las aplicaciones frontend son programas de software que se utilizan en una computadora o un dispositivo móvil, como la aplicación de una red social. En cambio, las aplicaciones backend son programas de software que se encargan de procesar los datos, de la lógica de negocio, así como también de las tareas de administración de recursos. El middleware actúa comunicando las aplicaciones frontend y backend para que la aplicación distribuida funcione sin inconvenientes.</p>

            <h3 id="arquitectura" class="text-primary my-5 ">¿Qué es la arquitectura del middleware?</h3>
            <p class="my-3">La arquitectura del software del middleware consiste en varios componentes que se comunican para crear una Data Pipeline. Los datos se mueven de una aplicación de conexión a la otra a través del middleware. El middleware procesa los datos para la compatibilidad. A continuación, se mencionan los componentes comunes del software del middleware:</p>


            <h4 class="mt-5 mb-2 text-indigo"><strong>Consola de administración</strong></h4>
            <p class="my-2">La consola de administración brinda información general de las actividades del sistema del middleware, las reglas del software y las configuraciones a los desarrolladores de software.</p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Interfaz del cliente</strong></h4>
            <p class="my-2">La interfaz del cliente es la parte externa del software del middleware que se comunica con las aplicaciones. Los desarrolladores utilizan funciones que les provee la interfaz del cliente para interactuar con otras aplicaciones, bases de datos u otros microservicios.</p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Interfaz interna del middleware</strong></h4>
            <p class="my-2">La Interfaz interna del middleware actúa como un pegamento de software que une a los diversos componentes. Los componentes del middleware utilizan la interfaz interna para funcionar de forma cohesiva con su propio protocolo.</p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Interfaz de la plataforma</strong></h4>
            <p class="my-2">La interfaz del middleware garantiza la compatibilidad del programa del middleware con varias plataformas. Esta contiene componentes de software que funcionan con diferentes tipos de sistemas operativos.</p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Administrador de contratos</strong></h4>
            <p class="my-2">El administrador de contratos define las reglas para el intercambio de datos en el sistema del middleware. Además, garantiza que las aplicaciones cumplan las reglas cuando se envíen datos con el middleware. Este envía una alerta, o una excepción, a la aplicación cuando esta vulnera reglas específicas. Por ejemplo, el administrador de contratos devolverá una excepción si la aplicación envía un número cuando se espera una palabra. </p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Administrador de sesión</strong></h4>
            <p class="my-2">El administrador de sesión configura un canal de comunicación seguro entre las aplicaciones y el middleware. Esto asegura que la comunicación fluya sin inconvenientes y almacena los registros de la actividad de los datos para elaborar informes.</p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Administrador de base de datos</strong></h4>
            <p class="my-2">Algunos tipos de middleware incluyen también un administrador de base de datos. El administrador de base de datos es responsable de la integración con los diferentes tipos de base de datos, según se necesite. </p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Supervisor del tiempo de ejecución</strong></h4>
            <p class="my-2">El supervisor del tiempo de ejecución brinda una supervisión continua de los movimientos de los datos en el middleware. Este detecta y reporta actividades inusuales a los desarrolladores. </p>

            <h3 id="funcionamiento" class="text-primary my-5 ">¿Cómo funciona el middleware?</h3>
            <p class="my-3">El middleware sintetiza el proceso de comunicación subyacente entre los componentes. Esto quiere decir que la aplicación frontend solo se comunica con el middleware y no tiene que aprender el lenguaje de los otros componentes de software de backend.</p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Marco de mensajería</strong></h4>
            <p class="my-2">Un marco de mensajería facilita el intercambio de datos entre aplicaciones frontend y backend. Entre los marcos más comunes, se incluyen los siguientes:</p>

            <ul class="list-group mt-5">
              <li class="list-group-item"><i class="bi bi-check2-square px-2 text-indigo"></i>Notación de objetos JavaScript (JSON)</li>
              <li class="list-group-item"><i class="bi bi-check2-square px-2 text-indigo"></i>Transferencia de estado representacional (REST API)</li>
              <li class="list-group-item"><i class="bi bi-check2-square px-2 text-indigo"></i>Lenguaje de marcado extensible (XML)</li>
              <li class="list-group-item"><i class="bi bi-check2-square px-2 text-indigo"></i>Servicios web</li>
              <li class="list-group-item"><i class="bi bi-check2-square px-2 text-indigo"></i>Protocolo simple de acceso a objetos (SOAP)</li>
            </ul>

            <p class="my-5">Los marcos de mensajería brindan una interfaz de comunicación común para aplicaciones en diferentes lenguajes y plataformas operativas. Las aplicaciones escriben y leen los datos en un formato estandarizado que les brinda el marco de mensajería. </p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Ejemplo del middleware</strong></h4>
            <p class="my-2">Por ejemplo, un servidor web es un middleware que conecta sitios web a la base de datos de backend. Cuando se envía un formulario en un sitio web, la computadora envía la solicitud en XML o JSON al servidor web. Luego, el servidor web ejecuta la lógica de negocio en función de la solicitud, recupera la información de las bases de datos o se comunica con otros microservicios por medio de protocolos diferentes.</p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Otras funciones del middleware</strong></h4>
            <p class="my-2">Además de actuar como intermediario entre aplicaciones de software, los programas del middleware también llevan a cabo las siguientes funciones:</p>

            <ul class="list-group mt-5">
              <li class="list-group-item"><i class="bi bi-check2-square px-2 text-indigo"></i>Proveen un canal de comunicación seguro entre aplicaciones distribuidas para que los sitios web envíen información confidencial de forma segura a las aplicaciones backend. </li>
              <li class="list-group-item"><i class="bi bi-check2-square px-2 text-indigo"></i>Administran la fluidez del tráfico y evitan abrumar a una aplicación o a un servidor en particular.</li>
              <li class="list-group-item"><i class="bi bi-check2-square px-2 text-indigo"></i>Automatizan y personalizan las respuestas a las solicitudes. Por ejemplo, el middleware clasifica y filtra los resultados antes de enviarlos a la aplicación frontend.</li>
            </ul>


            <h3 id="plataforma" class="text-primary my-5 ">¿Qué es el middleware de plataforma?</h3>
            <p class="my-3">El middleware de plataforma respalda el desarrollo de aplicaciones por medio de un sistema de herramientas y recursos administrados. Los desarrolladores utilizan el middleware de plataforma para compartir o transferir recursos entre las aplicaciones. A continuación, se presentan algunos ejemplos de recursos de middleware de plataforma:</p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Entornos en tiempo de ejecución</strong></h4>
            <p class="my-2">Un entorno en tiempo de ejecución es como un pequeño sistema operativo que permite la ejecución de un programa de software. Por ejemplo, las aplicaciones Java deben ejecutarse en el entorno en tiempo de ejecución de Java. Los desarrolladores pueden utilizar AWS Lambda para configurar un entorno de tiempo de ejecución para cualquier lenguaje de programación. </p>

            <h4 class="mt-5 mb-2 text-indigo">Servidores web</h4>
            <p class="my-3">Un servidor web es un programa de computación que recibe, procesa y responde a las solicitudes de los sitios web. Los desarrolladores web utilizan Amazon Lightsail para alojar y administrar servidores web para aplicaciones sencillas. </p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Sistemas de administración de contenidos</strong></h4>
            <p class="my-2">El sistema de administración de contenido es un software que crea, modifica, almacena y publica información digital. Por ejemplo, WordPress es un sistema de administración de contenido de código abierto para la creación de sitios web. </p>

            <h4 class="mt-5 mb-2 text-indigo"><strong>Contenedores</strong></h4>
            <p class="my-2">Un contenedor es un paquete de los códigos y los recursos necesarios de la aplicación listo para implementar. Los desarrolladores utilizan Amazon Elastic Container Service (Amazon ECS) para desplegar, administrar y escalar aplicaciones en contenedores. </p>


            <h3 id="nube" class="text-primary my-5">¿Qué es el middleware en la computación en la nube?</h3>
            <p class="my-3">La computación en la nube implica la creación y la implementación de aplicaciones nativas en la nube en infraestructuras diferentes. Los desarrolladores utilizan el middleware para tener acceso a los recursos de la nube sin verse abrumados por la complejidad de administrar las infraestructuras. Los desarrolladores implementan aplicaciones en la nube en contenedores en un alojamiento escalable basado en la nube, como Amazon Elastic Compute Cloud (Amazon EC2).</p>
