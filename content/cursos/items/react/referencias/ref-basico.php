          <article class="w-100 px-4 my-5 article">

            <h2 class="text-lila mt-5"><em>Concepto de prop en react</em></h2>
            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">En React, un prop es una propiedad que se utiliza para enviar datos entre componentes. Son una forma de comunicar información entre los componentes de una aplicación. </p>
            </div>
            <p class="pathway-extreme-p text-justify mt-4">Conceptualmente, los componentes son como las funciones de JavaScript. Aceptan datos de entrada (llamados props) y retornan elementos de React que describen lo que debería aparecer en la pantalla. Por ejemplo</p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                function Card(<span class="text-amarillo">props</span>) {
                  &nbsp;&nbsp;&nbsp;return (
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;div className="card"&gt;
                      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;h2&gt;{<span class="text-amarillo">props</span>.<span class="text-tomato">title</span>}&lt;/h2&gt;
                      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;div&gt;{<span class="text-amarillo">props</span>.<span class="text-tomato">children</span>}&lt;/div&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;/div&gt;
                  &nbsp;&nbsp;&nbsp;)
                }
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Y luego podemos usarlo de la siguiente forma:</p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                &lt;<span class="text-blue">Card</span> <span class="text-tomato">title</span>="Título de la tarjeta"&gt;
                  &nbsp;&nbsp;&nbsp;&lt;p&gt;Contenido de la tarjeta&lt;/p&gt;
                &lt;/<span class="text-blue">Card</span>&gt;
              </code>
            </pre>
            <div class="nota my-5">
              <div>
                <span class="text-tomato"><i class="bi bi-exclamation-circle-fill"></i>Nota:</span>
              </div>
              <div>
                <p>La prop <span class="text-lila_2"><em>children</em></span> es una prop especial que se pasa a los componentes. Es un objeto que contiene los elementos que envuelve un componente.
                  <br />En este caso, la prop <span class="text-lila_2"><em>children</em></span> contiene el elemento &lt;p&gt;Contenido de la tarjeta&lt;/p&gt;.
                  <br />Conocer y saber usar la prop <span class="text-lila_2"><em>children</em></span> es muy importante para crear componentes reutilizables en React.
                </p>
              </div>
            </div>

            <p class="pathway-extreme-p text-justify mt-4">El ejemplo más claro es un componente que tenga un mensaje de bienvenida a un usuario.</p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-success">const</span> <span class="text-skyblue_2">Greetings</span> = () => &lt;<span class="text-danger">h1</span>&gt;Hola Fulano!&lt;/<span class="text-danger">h1</span>&gt;
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Una <span class="text-verde">Prop</span> contendría el valor de <em>Fulano</em></p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-success">const</span> <span class="text-skyblue_2">Greetings</span> = (<span class="text-amarillo">props</span>) => &lt;<span class="text-danger">h1</span>&gt;Hola {<span class="text-amarillo">props</span>.<span class="text-lila_2">name</span>}!&lt;/<span class="text-danger">h1</span>&gt;
                
                <span class="comentario"><em>implementacion</em></span>
                &lt;<span class="text-skyblue_2">Greetings</span> <span class="text-lila_2">name</span>=”José Luis” /&gt;
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Las props, abreviación de “properties”, permiten enviar datos de un componente padre a uno hijo definiendo una relación jerárquica entre los componentes. A diferencia del estado, que es mutable y cambia dentro de un componente específico, las props son inmutables. Lo que significa que el componente que las recibe no puede modificarlas.</p>
            <p class="pathway-extreme-p text-justify mt-4">En términos prácticos, si un componente necesita recibir información para funcionar, la recibe vía props.</p>

            <h3 id="" class="my-5">Características de las props</h3>
            <ul class="my-5">
              <li class="my-3 fs-5"><i class="bi bi-check2-square" style="color: #fd0;"></i>Son inmutables, es decir, no se pueden modificar.</li>
              <li class="my-3 fs-5"><i class="bi bi-check2-square" style="color: #fd0;"></i>Pueden tener un valor por defecto.</li>
              <li class="my-3 fs-5"><i class="bi bi-check2-square" style="color: #fd0;"></i>Se pueden marcar como obligatorias, cuando un componenente no puede funcionar sin recibir una prop.</li>
              <li class="my-3 fs-5"><i class="bi bi-check2-square" style="color: #fd0;"></i>Permiten pasar cualquier valor de JavaScript, como objetos, arrays y funciones.</li>
              <li class="my-3 fs-5"><i class="bi bi-check2-square" style="color: #fd0;"></i>Son similares a los atributos HTML.</li>
            </ul>
            <p class="pathway-extreme-p text-justify mt-4">En JSX, las props se ven como los atributos de los elementos HTML:</p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                &lt;<span class="text-amarillo">Btn</span> <span class="text-amarillo">value</span>=”Enviar” /&gt;
