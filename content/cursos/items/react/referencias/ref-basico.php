            <h2 class="text-lila mt-5"><em>Importar y exportar componentes</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">La magia de los componentes reside en su reusabilidad: puedes crear componentes que se componen a su vez de otros componentes.</p>
            </div>

            <p class="pathway-extreme-p text-justify mt-4"> Pero mientras anidas más y más componentes, a menudo tiene sentido comenzar a separarlos en diferentes archivos. Esto permite que tus archivos se mantengan fáciles de localizar y puedas reutilizar componentes en más lugares.</p>

            <p class="pathway-extreme-p text-justify mt-4">En archivo de componente raíz por defecto es el llamado App.js. No obstante, dependiendo de tu configuración, tu componente raíz podría estar en otro archivo. Si utilizas un framework con enrutamiento basado en archivos, como Next.js, tu componente raíz será diferente para cada página.</p>

            <h3 id="" class="my-5">Exportar e importar un componente </h3>
