            <h3 id="" class="my-5">Anidar y organizar componentes</h3>
            <p class="pathway-extreme-p text-justify mt-4">Los componentes son funciones regulares de JavaScript, por lo que puedes tener múltiples componentes en el mismo archivo. Esto es conveniente cuando los componentes son relativamente pequeños o están estrechamente relacionados entre sí. Si este archivo se torna abarrotado, siempre puedes mover Profile a un archivo separado.</p>

            <p class="pathway-extreme-p text-justify mt-4">Dado que los componentes Profile se renderizan dentro de Gallery (¡incluso varias veces!) podemos decir que Gallery es un componente padre, que renderiza cada Profile como un “hijo”. Este es la parte mágica de React: puedes definir un componente una vez, y luego usarlo en muchos lugares y tantas veces como quieras.</p>

