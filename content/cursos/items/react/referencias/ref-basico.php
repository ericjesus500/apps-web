            <h2 class="text-lila mt-5"><em>¿Qué es un componente en React?</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">Un componente es una pieza independiente de nuestra UI. Un componente puede estar constituido por una o más unidades llamadas elementos. Por ejemplo, el div, img, span, p, h1, todos estos son elementos.</p>
            </div>

            <p class="pathway-extreme-p text-justify mt-4">En la Web, HTML nos permite crear documentos estructurados con su conjunto integrado de etiquetas como &lt;h1&gt; y &lt;li&gt;:</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                &lt;article&gt;
                  &nbsp;&nbsp;&nbsp;&lt;h1&gt;Mi primer componente&lt;/h1&gt;
                  &nbsp;&nbsp;&nbsp;&lt;ol&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;li&gt;Componentes: Bloques de construcción de la UI&gt;/li&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;li&gt;Definiendo un componente&lt;/li&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;li&gt;Usando un componente&lt;/li&gt;
                  &nbsp;&nbsp;&nbsp;&lt;/ol&gt;
                &lt;/article&gt;
              </code>
            </pre>

            <p class="pathway-extreme-p text-justify mt-4">Este marcado representa un artículo &lt;article&gt;, su encabezado &lt;h1&gt;, y una (abreviada) tabla de contenidos representada como una lista ordenada &lt;ol&gt;. Un marcado como este, combinado con CSS para los estilos y JavaScript para la interactividad, están detrás de cada barra lateral, avatar, modal, menú desplegable y cualquier otra pieza de UI que ves en la web.</p>

            <p class="pathway-extreme-p text-justify mt-4">React te permite combinar tu marcado, CSS y JavaScript en “componentes” personalizados, elementos reutilizables de UI para tu aplicación. El código de la tabla de contenidos que viste arriba pudo haberse transformado en un componente &lt;TableOfContents /&gt; que podrías renderizar en cada página. Por detrás, seguiría utilizando las mismas etiquetas HTML como &lt;article&gt;, &lt;h1&gt;, etc.</p>

            <p class="pathway-extreme-p text-justify mt-4">De la misma forma que con las etiquetas HTML, puedes componer, ordenar y anidar componentes para diseñar páginas completas. Por ejemplo la página de documentación que estás leyendo está hecha de componentes de React:</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                &lt;PageLayout&gt;
                  &nbsp;&nbsp;&nbsp;&lt;NavigationHeader&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;SearchBar /&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;Link to="/docs"&gt;Documentación&lt;/Link&gt;
                  &nbsp;&nbsp;&nbsp;&lt;/NavigationHeader&gt;
                  &nbsp;&nbsp;&nbsp;&lt;Sidebar /&gt;
                  &nbsp;&nbsp;&nbsp;&lt;PageContent&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;TableOfContents /&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;DocumentationText /&gt;
                  &nbsp;&nbsp;&nbsp;&lt;/PageContent&gt;
                &lt;/PageLayout&gt;
              </code>
            </pre>

            <p class="pathway-extreme-p text-justify mt-4">Básicamente un componente de React es una función de JavaScript a la que le puedes agregar un poco de marcado (markup). Los componentes pueden ser tan pequeños como un botón, o tan grandes como una página entera. </p>

            <p class="pathway-extreme-p text-justify mt-4">Por ejemplo</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                const Title = () => &lt;h1&gt;My Title&lt;/h1&gt;

                const Header = () => {
                  &nbsp;&nbsp;&nbsp;return (
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;header&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;Title /&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;/header&gt;
                  &nbsp;&nbsp;&nbsp;)
                }
              </code>
            </pre>

            <p class="pathway-extreme-p text-justify mt-4">Una analogía de los componentes son las piezas de lego. Una pieza de lego es independiente, reusable y tiene su propia estructura (forma y color).</p>

            <p class="pathway-extreme-p text-justify mt-4">Un componente tiene las siguientes características</p>

            <ul class="my-5">
              <li class="my-3 fs-5"><i class="bi bi-book-half" style="color: #fd0;"></i>Es aislado.</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half" style="color: #fd0;"></i>Es reusable.</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half" style="color: #fd0;"></i>Define la estructura (html), el estilo (css) y el comportamiento (js).</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half" style="color: #fd0;"></i>Los componentes permiten separar la interfaz de usuario en piezas independientes, reutilizables y pensar en cada pieza de forma aislada.</li>
            </ul>

