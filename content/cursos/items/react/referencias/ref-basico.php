            <h2 class="text-lila mt-5"><em>JSX: Poniendo marcado dentro de JavaScript</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">JSX es una extensión de sintaxis para JavaScript que permite escribir marcado similar a HTML dentro de una archivo JavaScript. Aunque hay otras formas de escribir componentes, la mayoría de los desarrolladores de React prefieren la concisión de JSX, y la mayoría de las bases de código lo usan.</p>
            </div>
            <p class="pathway-extreme-p text-justify mt-4">La Web se ha construido sobre HTML, CSS, y JavaScript. Durante muchos años, los desarrolladores web mantuvieron el contenido en HTML, el diseño en CSS, y la lógica en JavaScript, ¡a menudo en archivos separados!. El contenido se marcó dentro del HTML mientras que la lógica de la pagina vivía por separado en JavaScript</p>
            <p class="pathway-extreme-p text-justify mt-4">Pero, a medida que la Web se volvió más interactiva, la lógica determinó cada vez más el contenido. ¡JavaScript estaba a cargo del HTML! Esto es la razón por la que en React, la lógica de renderizado y el marcado viven juntos en el mismo lugar: componentes.</p>
            <div class="text-center my-5">
              <img src="../src/img/react_jsx.png" alt="react jsx" class="img-fluid">
            </div>
            <p class="pathway-extreme-p text-justify mt-4">JSX se parece mucho a HTML, pero es un poco más estricto y puede mostrar información dinámica. La mejor manera de comprender esto es convertir algunos marcados HTML en marcado JSX.</p>
            <h3 id="" class="my-5">Las Reglas de JSX</h3>
            <h4 class="resaltado">1. Devolver un solo elemento raíz </h4>
            <p class="pathway-extreme-p text-justify mt-4">Para devolver múltiples elementos de un componente, envuélvelos con una sola etiqueta principal.</p>
            <p class="pathway-extreme-p text-justify mt-4">Por ejemplo, puedes usar un &lt;div&gt;:</p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                &lt;<span class="text-amarillo">div</span>&gt;
                  &nbsp;&nbsp;&nbsp;&lt;h1&gt;Tareas Pendientes de Hedy Lamarr&lt;/h1&gt;
                  &nbsp;&nbsp;&nbsp;&lt;img 
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;src="https://i.imgur.com/yXOvdOSs.jpg" 
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;alt="Hedy Lamarr" 
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;class="photo"
                  &nbsp;&nbsp;&nbsp;&gt;
                  &nbsp;&nbsp;&nbsp;&lt;ul&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;...
                  &nbsp;&nbsp;&nbsp;&lt;/ul&gt;
                &lt;/<span class="text-amarillo">div</span>&gt;
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Si no deseas agregar un &lt;div&gt; adicional a tu marcado, puedes escribir &lt;&gt; y &lt;/&gt; en su lugar:</p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-tomato fs-5">&lt;&gt;</span>
                  &nbsp;&nbsp;&nbsp;&lt;h1&gt;Tareas Pendientes de Hedy Lamarr&lt;/h1&gt;
                  &nbsp;&nbsp;&nbsp;&lt;img 
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;src="https://i.imgur.com/yXOvdOSs.jpg" 
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;alt="Hedy Lamarr" 
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;class="photo"
                  &nbsp;&nbsp;&nbsp;&gt;
                  &nbsp;&nbsp;&nbsp;&lt;ul&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;...
                  &nbsp;&nbsp;&nbsp;&lt;/ul&gt;
                <span class="text-tomato fs-5">&lt;/&gt;</span>
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Esta etiqueta vacía se llama un Fragmento. Los Fragmentos te permiten agrupar cosas sin dejar ningún rastro en el árbol HTML del navegador.</p>

            <h4 class="resaltado">2. Cierra todas las etiquetas</h4>
            <p class="pathway-extreme-p text-justify mt-4">JSX requiere que las etiquetas se cierren explícitamente: las etiquetas de cierre automático como &lt;img&gt; deben convertirse en &lt;img /&gt;, y etiquetas envolventes como &lt;li&gt;naranjas deben convertirse como &lt;li&gt;naranjas&lt;/li&gt;.</p>

            <h4 class="resaltado">3. ¡camelCase todo la mayoría de las cosas! </h4>
            <p class="pathway-extreme-p text-justify mt-4">JSX se convierte en JavaScript y los atributos escritos en JSX se convierten en keys de objetos JavaScript. En tus propios componentes, a menudo vas a querer leer esos atributos en variables. Pero JavaScript tiene limitaciones en los nombres de variables. Por ejemplo, sus nombres no pueden contener guiones ni ser palabras reservadas como class.</p>
            <p class="pathway-extreme-p text-justify mt-4">Por eso, en React, muchos atributos HTML y SVG están escritos en camelCase. Por ejemplo, en lugar de <span class="text-lila">stroke-width</span> usa <span class="text-lila">strokeWidth</span>. Dado que class es una palabra reservada, en React escribes <span class="text-lila">className</span> en su lugar, con el nombre de la propiedad DOM correspondiente:</p>

