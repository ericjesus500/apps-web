            <h2 class="text-lila mt-5"><em>¿Qué es React</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">React es una biblioteca de JavaScript para renderizar interfaces de usuario (UI por sus siglas en inglés). La UI se construye a partir de pequeñas unidades como botones, texto e imágenes. React te permite combinarlas en componentes reutilizables y anidables. Desde sitios web hasta aplicaciones de teléfonos, todo en la pantalla se puede descomponer en componentes.</p>
            </div>

            <p class="pathway-extreme-p text-justify mt-4">Generalidades:</p>
            <ul class="my-5 list">
              <li class="my-3 fs-5"><i class="bi bi-book-half px-2" style="color: #fd0;"></i>React es una biblioteca Javascript para crear interfaces de usuario.</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half px-2" style="color: #fd0;"></i>React no es un framework (a diferencia de Angular o Vue).</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half px-2" style="color: #fd0;"></i>Está basado en componentes.</li>
            </ul>

            <h3 class="my-5">Instalar React</h3>
            <p class="pathway-extreme-p text-justify mt-4">Se puede instalar React utilizando <strong><em>Vite</em></strong></p>
            <p class="pathway-extreme-p text-justify mt-4">Vite requiere Node.js version 18+ o 20+. Sin embargo, algunas plantillas requieren una versión superior de Node.js para funcionar, por favor actualiza si tu gestor de paquetes te advierte sobre ello.</p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
              $ <span class="text-amarillo">npm</span> <span class="text-lila_2">create</span> <span class="text-tomato">vite@latest</span>                
              </code>
            </pre>

            <p class="pathway-extreme-p text-justify mt-4">¡Entonces sigue las instrucciones!</p>

            <p class="pathway-extreme-p text-justify mt-4">También puedes especificar directamente el nombre del proyecto y la plantilla que deseas usar a través de las opciones de línea de comandos adicionales. Por ejemplo, para montar un proyecto de Vite + Vue, ejecuta:</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="comentario"># npm 7+, se requiere guión doble extra:</span>
                <span class="text-amarillo">npm</span> <span class="text-lila_2">create</span> <span class="text-tomato">vite@latest</span> <span class="text-indigo_2">my-vue-app</span> -- --<span class="text-skyblue_2">template</span> <span class="text-warning">vue</span>
              </code>
            </pre>

            <h3 id="" class="my-5">React y jsx</h3>
            <p class="pathway-extreme-p text-justify mt-4">Jsx es una sintaxis que nos permite escribir componentes de react de una manera más declarativa.</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-info">const</span> <span class="text-primary">myComponent</span> = () => &lt;<span class="text-tomato">h1</span>&gt;Componente declarado con JSX&lt;/<span class="text-tomato">h1</span>&gt;
              </code>
            </pre>

            <p class="pathway-extreme-p text-justify mt-4">Parece HTML, pero en realidad es Javascript. Lo que estamos indicando aquí es: <em>Crea en la interfaz de usuario un elemento h1 cuyo contenido sea “Componente declarado con JSX”</em>.</p>

            <p class="pathway-extreme-p text-justify mt-4">Pero el navegador no sabe cómo interpretar la sintaxis de JSX. Por lo tanto, recurrimos a herramientas como webpack y babel para que nos transforme nuestro código en JSX en una sintaxis que el navegador si pueda reconocer.</p>

            <p class="pathway-extreme-p text-justify mt-4">Como ejemplo, este es el equivalente transpilado del código mostrado anteriormente.</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-info">const</span> <span class="text-primary">myComponent</span> = () => &lt;<span class="text-tomato">h1</span>&gt;Componente declarado con JSX&lt;/<span class="text-tomato">h1</span>&gt;

                <span class="comentario">// equivale a</span>

                <span class="text-info">const</span> <span class="text-primary">myComponent</span> = () => <span class="text-lila">React</span>.<span class="text-danger">createElement</span>('<span class="text-tomato">h1</span>', <span class="text-amarillo">null</span>, 'Componente declarado con JSX')
              </code>
            </pre>

            <h3 id="" class="my-5">Jsx y expresiones de javascript</h3>

            <p class="pathway-extreme-p text-justify mt-4">Ya que Jsx es javascript puro, es posible ejecutar expresiones de javascript dentro del mismo.</p>

            <p class="pathway-extreme-p text-justify mt-4">Una expresión de javascript es una sentencia del tipo:</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                1 + 1
                ‘hello’
                5 > 4
              </code>
            </pre>

            <p class="pathway-extreme-p text-justify mt-4">Considera este ejemplo.</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-info">const</span> <span class="text-skyblue_2">name</span> = 'john'

                <span class="text-info">const</span> <span class="text-primary">Grettings</span> = () => &lt;<span class="text-tomato">h1</span>&gt;Hello, {<span class="text-skyblue_2">name</span>}&lt;/<span class="text-tomato">h1</span>&gt;

                <span class="comentario">// equivalente transpilado sólo como ejemplo</span>

                <span class="text-info">const</span> <span class="text-skyblue_2">name</span> = 'john'

                <span class="text-info">const</span> <span class="text-primary">Grettings</span> = () => <span class="text-lila">React</span>.<span class="text-danger">createElement</span>('<span class="text-tomato">h1</span>', <span class="text-amarillo">null</span>, 'Hello, ', <span class="text-skyblue_2">name</span>)
              </code>
            </pre>

            <p class="pathway-extreme-p text-justify mt-4">JSX está más cerca de JavaScript, no de HTML, por lo que hay algunas diferencias clave a tener en cuenta al escribirlo.</p>

            <ul class="my-5">
              <li class="my-3 fs-5"><i class="bi bi-book-half px-2" style="color: #fd0;"></i><span class="text-lila"><em>className</em></span> se usa en lugar de <span class="text-decoration-underline"><em>class</em></span> para agregar clases CSS, ya que <span class="text-decoration-underline"><em>class</em></span> es una palabra clave reservada en JavaScript.</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half px-2" style="color: #fd0;"></i>Las propiedades y métodos en JSX son camelCase.</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half px-2" style="color: #fd0;"></i>Las etiquetas de cierre automático deben terminar en una barra inclinada, p. Ej. <img /></li>
            </ul>

            <p class="pathway-extreme-p text-justify mt-4">Las expresiones de JavaScript también se pueden incrustar dentro de JSX usando llaves, incluidas variables, funciones y propiedades.</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-info">const</span> <span class="text-primary">App</span> = () => {
                  &nbsp;&nbsp;&nbsp;<span class="text-danger">const</span> <span class="text-lila_2">titulo</span> = "Mi primera APP";
                  &nbsp;&nbsp;&nbsp;<span class="text-danger">return</span> (
                      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;<span class="text-danger">div</span> <span class="text-amarillo">className</span> = "<span class="text-warning">container</span>"&gt;
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;<span class="text-danger">h1</span> <span class="text-amarillo">className</span>="<span class="text-warning">text-primary</span>"&gt;{<span class="text-lila_2">titulo</span>}&lt;/<span class="text-danger">h1</span>&gt;
                      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;/<span class="text-danger">div</span>&gt;
                  &nbsp;&nbsp;&nbsp;);
                };

              <span class="text-danger">export default</span>  <span class="text-primary">App</span>;
              </code>
            </pre>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-info">const</span> <span class="text-primary">App</span> = () => {
                    &nbsp;&nbsp;&nbsp;<span class="text-danger">const</span> <span class="text-skyblue_2">titulo</span> = "Mi primera APP";
                    &nbsp;&nbsp;&nbsp;<span class="text-danger">const</span> <span class="text-lila_2">colores</span> = {
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span class="text-warning">primary</span>: "text-primary",
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span class="text-warning">info</span>: "text-info",
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span class="text-warning">danger</span>: "text-danger",
                    &nbsp;&nbsp;&nbsp;};

                    &nbsp;&nbsp;&nbsp;<span class="text-danger">return</span> (
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;<span class="text-danger">div</span> <span class="text-amarillo">className</span>="container"&gt;
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;<span class="text-danger">h1</span> <span class="text-amarillo">className</span>={<span class="text-lila_2">colores</span>.<span class="text-warning">primary</span>}&gt;{<span class="text-skyblue_2">titulo</span>}&lt;/<span class="text-danger">h1</span>&gt;
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;/<span class="text-danger">div</span>&gt;
                    &nbsp;&nbsp;&nbsp;);
                };

                <span class="text-danger">export default</span> <span class="text-primary">App</span>;
              </code>
            </pre>


            <h3 class="my-5">Jsx if y else</h3>
            <p class="pathway-extreme-p text-justify mt-4">En React, no hay una sintaxis especial para escribir condicionales. En cambio, usarás las mismas técnicas que usas al escribir código regular de JavaScript. Por ejemplo, puedes usar una sentencia if para incluir JSX condicionalmente:</p>

            <p class="pathway-extreme-p text-justify mt-4">Ejemplo 1</p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-danger">const</span> <span class="text-lila">Grettings</span> = () => {
                  &nbsp;&nbsp;&nbsp;<span class="text-danger">let</span> <span class="text-amarillo">name</span> = "Jesus";
                  &nbsp;&nbsp;&nbsp;<span class="text-danger">if</span> (<span class="text-amarillo">name</span>) {
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span class="text-danger">return</span> &lt;<span class="text-danger">h1</span>&gt;Hello {<span class="text-amarillo">name</span>}&lt;/<span class="text-danger">h1</span>&gt;;
                  &nbsp;&nbsp;&nbsp;} <span class="text-danger">else</span> {
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span class="text-danger">return</span> &lt;<span class="text-danger">h1</span>&gt;Hello?&lt;/<span class="text-danger">h1</span>&gt;;
                  &nbsp;&nbsp;&nbsp;}
                };

                <span class="text-danger">export default</span> <span class="text-lila_2">function</span> <span class="text-skyblue_2">MyApp</span>() {
                  &nbsp;&nbsp;&nbsp;<span class="text-danger">return</span> (
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;&gt;
                      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;<span class="text-danger">div</span>&gt;
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;<span class="text-danger">h1</span>&gt;Bienvenido a mi aplicación&lt;/<span class="text-danger">h1</span>&gt;
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;<span class="text-lila">Grettings</span> /&gt;
                      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;/<span class="text-danger">div</span>&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;/&gt;
                  &nbsp;&nbsp;&nbsp;);
                }

              </code>
            </pre>

            <p class="pathway-extreme-p text-justify mt-4">Ejemplo 2</p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-danger">let</span> <span class="text-warning">content</span>;
                <span class="text-danger">if</span> ( <span class="text-amarillo">isLoggedIn</span> ) {
                  &nbsp;&nbsp;&nbsp;<span class="text-warning">content</span> = &lt;<span class="text-skyblue_2">AdminPanel</span> /&gt;;
                } <span class="text-danger">else</span> {
                  &nbsp;&nbsp;&nbsp;<span class="text-warning">content</span> = &lt;<span class="text-skyblue_2">LoginForm</span> /&gt;;
                }
                <span class="text-danger">return</span> (
                  &nbsp;&nbsp;&nbsp;&lt;<span class="text-danger">div</span>&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;{<span class="text-warning">content</span>}
                  &nbsp;&nbsp;&nbsp;&lt;/<span class="text-danger">div</span>&gt;
                );
              </code>
            </pre>

            <p class="pathway-extreme-p text-justify mt-4">Si se prefiere un código más compacto, se puede utilizar el operador <span class="text-tomato"><em>?</em></span> condicional. A diferencia de if, funciona dentro de JSX:</p>

            <p class="pathway-extreme-p text-justify mt-4">Ejemplo 3</p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-danger">const</span> <span class="text-warning">user</span> = false;

                <span class="text-danger">const</span> <span class="text-warning">SaludoBienvenida</span> = () => &lt;<span class="text-danger">h2</span> <span class="text-info">className</span>="<span class="text-amarillo">text-warning</span>"&gt;Bienvenido!&lt;/<span class="text-danger">h2</span>&gt;;

                <span class="text-danger">const</span> <span class="text-warning">SaludoDespedida</span> = () => (
                  &nbsp;&nbsp;&nbsp;&lt;<span class="text-danger">h2</span> <span class="text-info">className</span>="<span class="text-amarillo">text-secondary</span>"&gt;Usuario offline&lt;/<span class="text-danger">h2</span>&gt;
                );

                <span class="text-danger">export default</span> <span class="text-lila">function</span> <span class="text-skyblue_2">Test</span>() {
                  &nbsp;&nbsp;&nbsp;<span class="text-danger">return</span> (
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;&gt;
                      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;<span class="text-danger">h1</span> <span class="text-info">className</span>="<span class="text-amarillo">text-primary</span>"&gt;USER STATUS&lt;/<span class="text-danger">h1</span>&gt;

                      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;{<span class="text-warning">user</span> ? &lt;<span class="text-warning">SaludoBienvenida</span> /&gt; : &lt;<span class="text-warning">SaludoDespedida</span> /&gt;}
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;/&gt;
                  &nbsp;&nbsp;&nbsp;);
                }
              </code>
            </pre>

            <p class="pathway-extreme-p text-justify mt-4">Cuando no necesites la rama else, puedes también usar la sintaxis lógica <span class="font-monospace">&&</span>, más breve:</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                &lt;div&gt;
                  &nbsp;&nbsp;&nbsp;{<span class="text-skyblue_2">isLoggedIn</span> <span class="text-lila_2">&&</span> &lt;<span class="text-indigo">AdminPanel</span> /&gt;}
                &lt;/div&gt;
              </code>
            </pre>

            <h3 class="my-5">Jsx y atributos</h3>
            <p class="pathway-extreme-p text-justify mt-4">Ya que usamos Jsx para declarar elementos a mostrar en el navegador, también podemos especificar los atributos de dichos elementos.</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-info">const</span> <span class="text-primary">myElement</span> = &lt;<span class="text-danger">div</span> <span class="text-warning">id</span>="<span class="text-lila_2">element</span>"&gt;Mi elemento&lt;/<span class="text-danger">div</span>&gt;

                <span class="comentario">// equivalente transpilado</span>

                <span class="text-info">const</span> <span class="text-primary">myElement</span> = <span class="text-lila">React</span>.<span class="text-danger">createElement</span>(
                  &nbsp;&nbsp;&nbsp;'<span class="text-danger">div</span>',
                  &nbsp;&nbsp;&nbsp;{
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<span class="text-warning">id</span>: <span class="text-lila_2">'element</span>',
                  &nbsp;&nbsp;&nbsp;},
                  &nbsp;&nbsp;&nbsp;'Mi elemento'
                )
              </code>
            </pre>

            <div class="nota mb-5">
              <div>
                <span class="text-tomato"><i class="bi bi-exclamation-circle-fill"></i>Nota:</span>
              </div>
              <div>
                <p>Recordar que Jsx es javascript puro, no html,</p>
              </div>
            </div>

            <p class="pathway-extreme-p text-justify mt-4">Otros ejemplos:</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-info">const</span> <span class="text-primary">Avatar</span> = (<span class="text-warning">user</span>) => &lt;<span class="text-danger">img</span> src=<span class="text-warning">user</span>.<span class="text-amarillo">photo</span>/&gt;

                <span class="text-info">const</span> <span class="text-skyblue_2">getSum</span> = (n1, n2) => n1 + n2

                <span class="text-info">const</span> <span class="text-primary">Result</span> = () => &lt;<span class="text-danger">h1</span>&gt;{<span class="text-skyblue_2">getSum</span>(1, 2)}&lt;/<span class="text-danger">h1</span>&gt;
              </code>
            </pre>

            <h3 class="my-5">Jsx y childrens o hijos.</h3>
            <p class="pathway-extreme-p text-justify mt-4">Podemos anidar tantos elementos como deseemos con jsx.</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-info">const</span> <span class="text-primary">myElement</span> = (<span class="text-warning">post</span>) => {
                  &nbsp;&nbsp;&nbsp;<span class="text-danger">return</span> (
