            <h2 class="text-lila my-5"><em>Definir un componente</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">Un componente de React es una función de JavaScript a la que puedes agregar marcado.</p>
            </div>

            <div class="text-center my-5">
              <img src="../src/img/definir_componente.png" alt="definir componente" class="img-fluid">
            </div>

            <h3 id="" class="my-5">Cómo construir un componente</h3>
            <h4 class="resaltado">Paso 1: Exporta el componente</h4>
            <p class="pathway-extreme-p text-justify mt-4">El prefijo <span class="text-lila"><em>export default</em></span> es parte de la sintaxis estándar de Javascript (no es específico de React). Te permite marcar la función principal en un archivo para que luego puedas importarlas en otros archivos. </p>

            <h4 class="resaltado">Paso 2: Define la función</h4>
            <p class="pathway-extreme-p text-justify mt-4">Con <span class="text-lila"><em>function Profile() { }</em></span> defines una función con el nombre Profile.</p>

            <div class="nota mb-5">
              <div>
                <span class="text-tomato"><i class="bi bi-exclamation-circle-fill"></i>Nota:</span>
              </div>
              <div>
                <p>¡Los componentes de React son funciones regulares de JavaScript, pero sus nombres deben comenzar con letra mayúscula o no funcionarán!</p>
              </div>
            </div>

            <h4 class="resaltado">Paso 3: Agrega marcado </h4>

            <p class="pathway-extreme-p text-justify mt-4">El componente devuelve una etiqueta &lt;img /&gt; con atributos src y alt. &lt;img /&gt; se escribe como en HTML, ¡pero en realidad es JavaScript por detrás! Esta sintaxis se llama <span class="text-lila">JSX</span>, y te permite incorporar marcado dentro de JavaScript.</p>

            <p class="pathway-extreme-p text-justify mt-4">Las sentencias <span class="text-lila">return</span> se pueden escribir todo en una línea, como en este componente:</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-danger">return</span> &lt;<span class="text-warning">img</span> <span class="text-tomato">src</span>="https://i.imgur.com/MK3eW3As.jpg" <span class="text-tomato">alt</span>="Katherine Johnson" /&gt;;
              </code>
            </pre>

            <p class="pathway-extreme-p text-justify mt-4">Pero si tu marcado no está todo en la misma línea que la palabra clave <span class="text-lila">return</span>, debes ponerlo dentro de paréntesis como en este ejemplo:</p>

            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="text-tomato">return</span> (
                  &nbsp;&nbsp;&nbsp;&lt;<span class="text-danger">div</span>&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;<span class="text-danger">img</span> src="https://i.imgur.com/MK3eW3As.jpg" alt="Katherine Johnson" /&gt;
                  &nbsp;&nbsp;&nbsp;&lt;/<span class="text-danger">div</span>&gt;
                );
              </code>
            </pre>
            <div class="nota mb-5">
              <div>
                <span class="text-tomato"><i class="bi bi-exclamation-circle-fill"></i>Nota:</span>
              </div>
              <div>
                <p>¡Sin paréntesis, todo el código que está en las líneas posteriores al return serán ignoradas!</p>
