            <div class="alert alert-warning">
              <h5><i class="bi bi-book-half"></i> Un paso más allá!</h5>
              <p class="pathway-extreme-p text-justify mt-4">Una aplicación React comienza en un componente “root” (raíz). Normalmente, se crea automáticamente al iniciar un nuevo proyecto. Por ejemplo, si usas CodeSandbox o si usas el framework Next.js, el componente root se define en pages/index.js. En estos ejemplos, has estado exportando componentes root.</p>
              <p class="pathway-extreme-p text-justify mt-4">La mayoría de las aplicaciones React usan componentes root. Esto significa que no solo usarás componentes para piezas reutilizables como botones, sino también para piezas más grandes como barras laterales, listas y, en última instancia, ¡páginas completas! Los componentes son una forma práctica de organizar el código de UI y el marcado, incluso si algunos de ellos solo se utilicen una vez.</p>
              <p class="pathway-extreme-p text-justify mt-4">Los frameworks basados en React llevan esto un paso más allá. En lugar de utilizar un archivo HTML vacío y dejar que React “se encargue” de gestionar la página con JavaScript, también generan el HTML automáticamente a partir de tus componentes React. Esto permite que tu aplicación muestre algún contenido antes de que se cargue el código JavaScript.</p>
              <p class="pathway-extreme-p text-justify mt-4">Aún así, muchos sitios web sólo usan React para añadir interactividad a páginas HTML existentes. Tienen muchos componentes root en lugar de uno solo para toda la página. Puedes utilizar la cantidad de React que necesites.</p>
