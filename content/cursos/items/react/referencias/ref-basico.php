            <h3 id="" class="my-5">Reenviar props con la sintaxis de propagación JSX </h3>
            <p class="pathway-extreme-p text-justify mt-4">A veces, pasar props se vuelve muy repetitivo:</p>
            <p class="pathway-extreme-p text-justify mt-4">No hay ningún problema en tener código repetitivo—ya que puede ser más legible. Sin embargo, en ocasiones, es posible que prefieras ser más conciso. Algunos componentes reenvían todas sus props a sus hijos, como lo hace Profile con Avatar. Dado que no utilizan directamente ninguna de sus props, tiene sentido utilizar una sintaxis de “propagación” más concisa:</p>

            <div class="row">
              <div class="col-sm-12 col-md-6 col-lg-6">
                <p>Lo tradicional:</p>
                <pre class="pre-content-list bg-blue_2 my-5">
                  <code>
                    function <span class="text-skyblue_2">Profile</span>({ <span class="text-amarillo">person</span>, <span class="text-amarillo">size</span>, <span class="text-amarillo">isSepia</span>, <span class="text-amarillo">thickBorder</span> }) {
                      &nbsp;&nbsp;&nbsp;return (
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;div className="card"&gt;
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;<span class="text-tomato">Avatar</span>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;person={<span class="text-amarillo">person</span>}
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;size={<span class="text-amarillo">size</span>}
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;isSepia={<span class="text-amarillo">isSepia</span>}
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;thickBorder={<span class="text-amarillo">thickBorder</span>}                          
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;/&gt;
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;/div&gt;
                      &nbsp;&nbsp;&nbsp;);
                    }
                  </code>
                </pre>
              </div>
              <div class="col-sm-12 col-md-6 col-lg-6">
                <p>Usando sintaxis de "Propagación":</p>
                <pre class="pre-content-list bg-blue_2 my-5">
                  <code>
                    function <span class="text-skyblue_2">Profile</span>(<span class="text-amarillo">props</span>) {
                      &nbsp;&nbsp;&nbsp;return (
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;div className="card"&gt;
                          &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;<span class="text-tomato">Avatar</span> {...<span class="text-amarillo">props</span>} /&gt;
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;/div&gt;
                      &nbsp;&nbsp;&nbsp;);
                    }
                  </code>
                </pre>
              </div>
            </div>
            <p class="pathway-extreme-p text-justify mt-4">Esto permite reenviar todas las props de <span class="text-skyblue_2">Profile</span> a <span class="text-tomato">Avatar</span> sin la necesidad de especificar cada una de ellas.</p>

            <div class="nota my-5">
              <div>
                <span class="text-tomato"><i class="bi bi-exclamation-circle-fill"></i>Nota:</span>
              </div>
              <div>
                <p>Recuerda utilizar la sintaxis de propagación con moderación. Si estás utilizando esta sintaxis en cada componente, es probable que algo no esté correctamente estructurado. En muchos casos, esto sugiere que deberías dividir tus componentes y pasar los hijos como elementos JSX separados. </p>
              </div>
            </div>

