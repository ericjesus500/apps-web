            <h4 class="resaltado my-5">Explicación: </h4>
            <p class="pathway-extreme-p text-justify mt-4">Las props te permiten considerar de forma independiente los componentes padre e hijo. Por ejemplo, puedes modificar las props <span class="text-primary_2">person</span> o <span class="text-primary_2">size</span> dentro de <span class="text-skyblue_2">App</span> sin preocuparte por cómo serán utilizadas por el componente <span class="text-skyblue_2">Avatar</span>. De manera similar, puedes cambiar la forma en que <span class="text-skyblue_2">Avatar</span> utiliza estas props sin necesidad de revisar <span class="text-skyblue_2">App</span>.</p>

            <p class="pathway-extreme-p text-justify mt-4">Considera las props como “controles” que puedes ajustar. Cumplen el mismo papel que los argumentos de una función—de hecho, ¡las props son el único argumento de tu componente! Las funciones de los componentes de React aceptan un único argumento, un objeto props:</p>

