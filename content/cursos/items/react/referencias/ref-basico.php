            <h3 id="" class="my-5">Las props cambian con el tiempo</h3>
            <p class="pathway-extreme-p text-justify mt-4">¡Las props no siempre son estáticas!, por tanto, un componente puede recibir props que cambian a lo largo del tiempo. Las props reflejan los datos de un componente en cualquier momento, y no sólo al inicio.</p>

            <h4 class="resaltado my-5">Las props son inmutables</h4>

            <p class="pathway-extreme-p text-justify mt-4">Sin embargo, las props son inmutables—un término de la informática que significa “inalterable”. Si un componente necesita cambiar sus props (por ejemplo, en respuesta a una interacción del usuario o nuevos datos), debe “solicitar” a su componente padre que le pase nuevas props—¡un nuevo objeto! Las props antiguas se descartarán y eventualmente el motor de JavaScript liberará la memoria que ocupaban.</p>

            <p class="pathway-extreme-p text-justify mt-4">No se debe intentar <span class="text-success">"cambiar las props"</span>, en su lugar debe <span class="text-success">"establecerse un estado"</span></p>

            <h3 id="" class="my-5">RESUMEN</h3>

            <ul class="my-5">
              <li class="my-3 fs-5"><i class="bi bi-book-half" style="color: #fd0;"></i>Para pasar props, simplemente agrégalas al JSX, de la misma forma en que lo harías con los atributos HTML.</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half" style="color: #fd0;"></i>Para acceder a las props, utiliza la sintaxis de desestructuración function Avatar({ person, size }).</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half" style="color: #fd0;"></i>Puedes asignar un valor predeterminado como size = 100, que se utiliza para las props faltantes y undefined.</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half" style="color: #fd0;"></i>Puedes reenviar todas las props con la sintaxis de propagación JSX
                &lt;Avatar {...props} /&gt;, ¡pero no abuses de ella!.</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half" style="color: #fd0;"></i>Todo JSX anidado como &lt;Card&gt;&lt;Avatar /&gt;&lt;/Card&gt; aparecerá como la prop children del componente Card.</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half" style="color: #fd0;"></i>Las props son instantáneas de solo lectura en el tiempo: cada renderizado recibe una nueva versión de las props.</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half" style="color: #fd0;"></i>No puedes cambiar las props. Si necesitas interactividad, tendrás que establecer un estado.</li>
            </ul>

