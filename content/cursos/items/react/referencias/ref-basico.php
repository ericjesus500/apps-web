            <h2 id="def_metodo" class=" text-lila mt-5"><em>JavaScript en JSX con llaves</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">JSX te permite escribir marcas similares a HTML dentro de un archivo JavaScript, manteniendo la lógica de renderizado y el contenido en el mismo lugar. A veces vas a querer agregar un poco de lógica JavaScript o hacer referencia a una propiedad dinámica dentro de ese marcado. En esta situación, puedes usar llaves en tu JSX para abrir una ventana a JavaScript</p>
