            <h4 class="resaltado">El esquema</h4>
            <p class="pathway-extreme-p text-justify mt-4">El esquema suele representar el protocolo es decir el mecanismo o método que se emplea para recuperar un recurso a través de una red de ordenadores. A veces, en vez de esquema se emplea el término servicio.</p>
            <p class="pathway-extreme-p text-justify mt-4">En el contexto de una url son equivalentes los esquemas más conocidos son http, https, ftp, mailto y file.</p>
            <p class="pathway-extreme-p text-justify mt-4">Las urls son un elemento básico de la web ya que son las que definen los hiperenlaces o hipervínculos y permiten definir el hipertexto que permite relacionar información de diversas fuentes por medio de enlaces.</p>
            <p class="pathway-extreme-p text-justify mt-4">Es necesari precisar que desde 1994 en los estándares de internet el concepto de url ha sido incorporado dentro del más general: URI uniforme resorts identifier, en español identificador uniforme de recurso pero el término url aún se utiliza ampliamente</p>

