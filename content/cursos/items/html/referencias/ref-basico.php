            <h2 class="text-lila mt-5"><em>El Hipertexto y la Hipermedia</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">El concepto de Hipertexto se le atribuye a Paul Tollett que es considerado el fundador de la bibliografía y la documentación.</p>
            </div>
            <h3 id="" class="mt-5">El Hipertexto</h3>
            <p class="pathway-extreme-p text-justify mt-4">El Hipertexto es un conjunto estructurado de textos, gráficos, etcétera unidos entre sí por enlaces y conexiones lógicas. El Hipertexto permite saltar de un punto a otro en un mismo texto o a otro texto a través de referencias, de este modo en lugar de leer el texto de forma continua, en el hipertexto ciertos términos están relacionados y el texto se puede leer siguiendo diferentes caminos.</p>
            <p class="pathway-extreme-p text-justify mt-4">Las relaciones en el hipertexto se establecen entre lo que se suele llamar como referencias, enlaces, vínculos o hipervínculos.</p>

            <h3 id="" class="mt-5">La hipermedia</h3>
            <p class="pathway-extreme-p text-justify mt-4">El término hiper media no figura en el diccionario de la lengua española pero podemos buscar un término relacionado con hipermedia: <span class="text-lila"><em>multimedia</em></span></p>
            <p class="pathway-extreme-p text-justify mt-4">Según el diccionario de la lengua española, multimedia es un adjetivo que significa que utiliza conjunta y simultáneamente diversos medios como imágenes sonidos y texto en la transmisión de una información, por tanto, un sistema multimedia es un sistema de comunicación en el que se emplean dos o más medios de comunicación distintos de forma concurrente</p>
            <h4 class="resaltado">Características principales de la multilemdia</h4>
            <ul class="my-5">
              <li class="my-3 fs-5"><i class="bi bi-book-half px-2" style="color: #fd0;"></i>Un sistema multimedia puede integrar texto, voz, audio, fotografías, gráficos interactivos, vídeos, realidad virtual y otros.</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half px-2" style="color: #fd0;"></i>Un sistema multimedia proporciona una gran riqueza y una mayor flexibilidad a la hora de comunicar la información.</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half px-2" style="color: #fd0;"></i>La calidad de multimedia no está restringida al mundo de los ordenadores así por ejemplo un libro acompañado de un cd de música ya es una obra multimedia.</li>
            </ul>
            <p class="pathway-extreme-p text-justify mt-4">Para algunos autores, hipermedia es un término que nace de la unión del hipertexto más la multimedia, por tanto, si juntamos las definiciones de hipertexto y multimedia podemos obtener la siguiente definición de hipermedia: </p>
            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">Conjunto estructurado de diversos medios como textos, gráficos, imágenes y sonidos unidos entre sí por enlaces y conexiones lógicas para la transmisión de una información</p>
            </div>
            <p class="pathway-extreme-p text-justify mt-4">Existen muchos sistemas que se basan en el hipertexto y la híper media pero la <span class="text-lila"><em>web</em></span> es el sistema más conocido y por eso se ha convertido en sinónimo de hipertexto e hipermedia.</p>

          </article>

