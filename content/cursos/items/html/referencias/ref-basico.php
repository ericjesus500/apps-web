            <h2 class="text-lila mt-5"><em>HTML HyperText Markup Language</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">A finales de 1990 la web era muy distinta a como se le conoce hoy en el año 2025; sólo tenían texto.</p>
            </div>

            <p class="pathway-extreme-p text-justify mt-4">HTML el lenguaje de marcado o etiquetado que se emplea para crear las páginas web ha evolucionado poco a poco y se han ido desarrollando sucesivas versiones a la versión inicial del lenguaje se añadieron nuevas características como las imágenes las tablas o los marcos que permitían dividir las páginas web en varias partes</p>
            <p class="pathway-extreme-p text-justify mt-4">las páginas web fueron evolucionando y cada vez contenían más imágenes, los diseñadores gráficos se incorporaron al desarrollo de las páginas web y se desarrolló una nueva disciplina: <span class="text-lila"><em>el diseño web</em></span>.</p>
            <p class="pathway-extreme-p text-justify mt-4">Durante los primeros cinco años la web sólo servía para leer, para consumir contenidos, no existía mucha interacción con las páginas web sin embargo cuando se añadieron los <span class="text-lila"><em>formularios</em></span> la web comenzó a cambiar; los formularios permitieron un mayor grado de interacción entre el usuario y las páginas web</p>

            <p class="pathway-extreme-p text-justify mt-4">HTML incorpora la posibilidad de añadir nuevos tipos de contenidos a las páginas web como audio, vídeo o animaciones y los navegadores web se volvieron más rápidos y más potentes, todo ello ayudó a que las páginas web se transformarán en <span class="text-lila"><em>aplicaciones web</em></span> </p>

            <h3 id="" class="mt-5">Las aplicaciones web</h3>
            <p class="pathway-extreme-p text-justify mt-4">Las aplicaciones web (apps) permiten realizar a través de una página web las mismas tareas que tradicionalmente se realizaban mediante un software instalado en un ordenador a partir de un cd-rom.</p>

            <h4 class="resaltado">Ventajas de las aplicaciones web</h4>
            <ul class="my-5">
              <li class="my-3 fs-5"><i class="bi bi-book-half px-2" style="color: #fd0;"></i>No necesitan actualizarse por parte del usuario final. Cada vez que un usuario visita una aplicación web como por ejemplo gmail está haciendo uso de la última versión disponible</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half px-2" style="color: #fd0;"></i>Son independientes en su forma de uso, es decir, puedan ser usadas desde cualquier dispositivo que disponga de un navegador web como un ordenador una tableta o un teléfono móvil.</li>
              <li class="my-3 fs-5"><i class="bi bi-book-half px-2" style="color: #fd0;"></i>Otra ventaja importante es la ubicuidad, es decir, a las aplicaciones web se puede acceder y trabajar con la misma información desde cualquier sitio por ejemplo desde el ordenador del trabajo, desde el ordenador de casa o desde el teléfono móvil.</li>
            </ul>
            <p class="pathway-extreme-p text-justify mt-4">Estas ventajas de las aplicaciones web se resumen en el concepto de computación en la nube o <span class="text-lila"><em>cloud computing</em></span> qué va a hacer que el concepto de ordenador personal desaparezca.</p>
          </article>

