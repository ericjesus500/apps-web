            <h2 class="text-lila mt-5"><em>Las URLs</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">Tim Berners-Lee es considerado el padre de la web ya que desarrolló las tres tecnologías fundamentales de la web: HTML hypertext markup language que es el lenguaje de marcado o etiquetado que se emplea para crear, para escribir los documentos o páginas web; HTTP o hypertext transfer protocol que es el protocolo o lenguaje con el que se comunican el navegador y el servidor web y que se emplea para transmitir los documentos web por internet y la URL uniform resource locator que es el localizador de recursos.</p>
            </div>
            <h3 id="" class="mt-5">Formato básico de una URL</h3>
