            <h2 class="text-lila mt-5"><em>Qué es flexbox?</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">El Módulo de Caja Flexible, comúnmente llamado flexbox, fue diseñado como un modelo unidimensional de layout, y como un método que pueda ayudar a distribuir el espacio entre los ítems de una interfaz y mejorar las capacidades de alineación.</p>
            </div>
            <p class="pathway-extreme-ptext-justify mt-4">Cuando describimos a flexbox como unidimensional destacamos el hecho que flexbox maneja el layout en una sola dimensión a la vez — ya sea como fila o como columna. Esto contrasta con el modelo bidimensional del Grid Layout de CSS, el cual controla columnas y filas a la vez.</p>

            <h3 id="" class="mt-5"><em>Los dos ejes de flexbox</em></h3>
            <p class="pathway-extreme-ptext-justify mt-4">Cuando trabajamos con flexbox necesitamos pensar en términos de dos ejes — el eje principal y el eje cruzado. El eje principal está definido por la propiedad <span class="text-lila"><em>flex-direction</em></span>, y el eje cruzado es perpendicular a este. Todo lo que hacemos con flexbox está referido a estos dos ejes, por lo que vale la pena entender cómo trabajan desde el principio.</p>

            <ul class="">
              <li class="my-2 fs-5"><i class="bi bi-check2-square px-2 text-warning"></i>row</li>
              <li class="my-2 fs-5"><i class="bi bi-check2-square px-2 text-warning"></i>row-reverse</li>
              <li class="my-2 fs-5"><i class="bi bi-check2-square px-2 text-warning"></i>column</li>
              <li class="my-2 fs-5"><i class="bi bi-check2-square px-2 text-warning"></i>column-reverse</li>
            </ul>

            <h3 id="" class="mt-5"><em>El eje principal(main-axis)</em></h3>

            <p class="pathway-extreme-ptext-justify mt-4">El eje principal está definido por <span class="text-lila"><em>flex-direction</em></span>, que posee cuatro posibles valores:</p>

            <p class="pathway-extreme-ptext-justify mt-4">Si elegimos <span class="text-lila"><em>row</em></span> o <span class="text-lila"><em>row-reverse</em></span>, el eje principal correrá a lo largo de la fila según la dirección de la línea.</p>

            <div class="text-center my-5">
              <img src="../src/img/main-axis-1.PNG" alt="main-axis-1" class="img-fluid">
