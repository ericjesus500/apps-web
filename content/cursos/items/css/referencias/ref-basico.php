            <h2 id="mod_caja_concepto" class="text-lila mt-5"><em>Introducción al modelo de caja</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">Las tablas fueron los elementos que sin intención se volvieron la
                herramienta ideal utilizada por desarrolladores para crear y organizar cajas de contenido en
                Estilos CSS y modelos de caja 47 la pantalla.</p>
            </div>

            <p class="pathway-extreme-p text-justify mt-4">Usando etiquetas <em>&lt;div&gt;</em> y estilos CSS fue posible reemplazar la función de tablas y efectivamente separar la estructura HTML de la presentación. Con elementos <em>&lt;div&gt;</em> y CSS podemos crear cajas en la pantalla, posicionar estas cajas a un lado o a otro y darles un tamaño, color o borde específico entre otras características.</p>

            <!-- Propiedad float -->
            <div class="w-100">
              <h3 id="float" class="mt-5"><em>Propiedad float</em></h3>
              <p class="pathway-extreme-p text-justify mt-4">La propiedad de CSS <em><span class="text-verde">float</span></em> hace que el elemento flote hacia un lado o al otro en el espacio disponible.</p>
              <p class="pathway-extreme-p text-justify mt-4">Los elementos afectados por float actúan como elementos
                block (con la diferencia de que son ubicados de acuerdo al valor de esta propiedad y no el flujo normal del documento).</p>
              <div class="d-flex">
                <div class="text-center my-5 me-3 w-50">
                  <img src="../src/img/css_mod_caja.PNG" alt="mod_caja" class="img-fluid">
                </div>
                <div class="text-center my-5 w-50">
                  <img src="../src/img/css_estilos_seccion_columna.PNG" alt="estilos mod caja" class="img-fluid">
                </div>
              </div>

              <p class="pathway-extreme-p text-justify mt-4">En el ejemplo anterior, el contenido del elemento
                <em>&lt;section&gt;</em> estará situado a la izquierda de la pantalla con un tamaño de 660 pixeles, más 40 pixeles de margen, ocupando un espacio total de 700 pixeles de ancho.
              </p>
              <p class="pathway-extreme-p text-justify mt-4">La propiedad float del elemento <em>&lt;aside&gt;</em> también tiene el valor left (izquierda). Esto significa que la caja generada será movida al espacio disponible a su izquierda. Debido a que la caja previa creada por el elemento <em>&lt;section&gt;</em> fue también movida a la izquierda de la pantalla, ahora el espacio disponible será solo el que esta caja dejó libre. La nueva caja quedará ubicada en la misma línea que la primera pero a su derecha, ocupando el espacio restante en la línea, creando la segunda columna de nuestro diseño.</p>

              <p class="pathway-extreme-p text-justify mt-4">El ancho del elemento <em>&lt;aside&gt;</em> tendrá un resultado final de 220 pixeles más 40 pixeles agregados por la propiedad <em>padding</em> ya que sus márgenes fueron declarados 0 pixeles.</p>
            </div>

            <!-- Tamaño real -->
            <div class="w-100">
              <h3 id="tam_real" class="mt-5"><em>Tamaño real de un elemento</em></h3>
              <p class="pathway-extreme-p text-justify mt-4">El tamaño real de un elemento es calculado con la fórmula: <span class="text-amarillo">tamaño + márgenes + márgenes internos(padding) + bordes.</span></p>
            </div>

            <!-- propiedad clear -->
            <div class="w-100">
              <h3 id="clear" class="mt-5"><em>La propiedad clear</em></h3>
              <p class="pathway-extreme-p text-justify mt-4">Esta propiedad devuelve al documento su flujo normal.</p>

