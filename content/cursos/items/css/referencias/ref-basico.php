            <h2 id="css_html" class="text-lila mt-5"><em>CSS y HTML</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">CSS es un lenguaje que trabaja junto con HTML para proveer
                estilos visuales a los elementos del documento, como tamaño, color, fondo,
                bordes, etc…</p>
            </div>

            <p class="pathway-extreme-p text-justify mt-4">La nueva especificación de HTML (HTML5) no describe
              solo los nuevos elementos HTML o el lenguaje mismo. La web demanda diseño y
              funcionalidad, no solo organización estructural o definición de secciones. En este nuevo
              paradigma, HTML se presenta junto con CSS y Javascript como un único instrumento
              integrado.</p>

          </article>

          <!-- Estilos y estructura -->
          <article class="w-100 px-4 my-5 article">

            <h2 id="estilos_intro" class="text-lila mt-5"><em>Estilos y estructura</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">A pesar de que cada navegador garantiza estilos por defecto para cada uno de los elementos HTML, estos estilos no necesariamente satisfacen los requerimientos de cada
                diseñador.</p>
            </div>

            <h3 id="ele_block" class="mt-5">Elementos block</h3>
            <p class="pathway-extreme-p text-justify mt-4">Con respecto a la estructura, básicamente cada navegador ordena los elementos por defecto de acuerdo a su tipo: block (bloque) o inline (en línea). Esta clasificación está asociada con la forma en que los elementos son mostrados en pantalla.</p>

            <ul>
              <li><i class="bi bi-check2-square text-primary me-1"></i>Elementos block son posicionados uno sobre otro hacia abajo en la página.</li>
              <li><i class="bi bi-check2-square text-primary me-1"></i>Elementos inline son posicionados lado a lado, uno al lado del otro en la misma
                línea, sin ningún salto de línea a menos que ya no haya más espacio horizontal
                para ubicarlos.</li>
            </ul>

            <p class="pathway-extreme-p text-justify mt-4">Casi todos los elementos estructurales en nuestros documentos serán tratados por los navegadores como elementos block por defecto. Esto significa que cada elemento HTML que representa una parte de la organización visual (por ejemplo, &lt;section&gt;, &lt;nav&gt;, &lt;header&gt;, &lt;footer&gt;, &lt;div&gt;) será posicionado debajo del anterior.</p>

            <h3 id="mod_caja" class="mt-5">Modelos de caja</h3>
            <p class="pathway-extreme-p text-justify mt-4">Los navegadores consideran cada elemento HTML como una caja. Una página web es en realidad un grupo de cajas ordenadas siguiendo ciertas reglas. Estas reglas son establecidas por estilos provistos por los navegadores o por los diseñadores usando CSS.</p>
            <p class="pathway-extreme-p text-justify mt-4">CSS tiene un set predeterminado de propiedades destinados a sobrescribir los estilos provistos por navegadores y obtener la organización deseada. Estas propiedades no son específicas, tienen que ser combinadas para formar reglas que luego serán usadas para agrupar cajas y obtener la correcta disposición en pantalla. La combinación de estas reglas es normalmente llamada modelo o sistema de disposición. Todas estas reglas aplicadas juntas constituyen lo que se llama un modelo de caja.</p>

          </article>

