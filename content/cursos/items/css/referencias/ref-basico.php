              <p class="pathway-extreme-p text-justify mt-4">La propiedad <em><span class="text-verde">box-sizing</span></em> nos permite cambiar cómo el espacio total ocupado por un elemento en pantalla será calculado forzando a los navegadores a incluir en el ancho original los valores de las propiedades padding y border.</p>
              <p class="pathway-extreme-p text-justify mt-4">La propiedad box-sizing puede tomar dos valores. Por defecto es configurada como <em>content-box</em>, lo que significa que los navegadores agregarán los valores de padding y border al tamaño especificado por width y height. Usando el valor <em>border-box</em> en su lugar, este comportamiento es cambiado de modo que padding y border son incluidos dentro del elemento.</p>
