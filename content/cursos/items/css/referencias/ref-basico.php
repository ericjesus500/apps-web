            <h2 id="concepto_estilos" class="text-lila mt-5"><em>Conceptos básicos sobre estilos</em></h2>

            <div class="callout callout-warning my-5">
              <p class="pathway-extreme-p text-dark">Aplicar estilos a los elementos HTML cambia la forma en que estos son presentados en pantalla.</p>
            </div>

            <p class="pathway-extreme-p text-justify mt-4">Los navegadores proveen estilos por defecto que en la mayoría de los casos no son suficientes para satisfacer las necesidades de los diseñadores. Para cambiar esto, podemos sobrescribir estos estilos con los nuestros usando diferentes técnicas como las siguientes:</p>

            <ul>
              <li><i class="bi bi-check2-square text-primary me-1"></i>Estilos en línea</li>
              <li><i class="bi bi-check2-square text-primary me-1"></i>Estilos embebidos: es insertar los estilos en la cabecera del documento y luego usar referencias para afectar los elementos HTML correspondientes</li>
              <li><i class="bi bi-check2-square text-primary me-1"></i>Uando archivos externos</li>
            </ul>

            <!-- Referencias -->
            <div class="w-100">
              <h3 id="estilos_referencias" class="mt-5">Referencias</h3>
              <p class="pathway-extreme-p text-justify mt-4">Existen varios métodos para seleccionar cuáles elementos HTML serán afectados por las reglas CSS:</p>
              <ul>
                <li><i class="bi bi-check2-square text-primary me-1"></i>Referencia por la palabra clave del elemento</li>
                <li><i class="bi bi-check2-square text-primary me-1"></i>Referencia por el atributo id</li>
                <li><i class="bi bi-check2-square text-primary me-1"></i>Referencia por el atributo class</li>
              </ul>

              <h4 class="resaltado my-5 w-75">Refrenciando con palabra clave</h4>
              <p class="pathway-extreme-p text-justify mt-4">Al declarar las reglas CSS utilizando la palabra clave del elemento afectamos cada elemento de la misma clase en el documento. Por ejemplo, la siguiente regla cambiará los estilos de todos los elementos &lt;p&gt;:</p>

              <pre class="pre-content-list bg-blue_2 my-5">
                <code>
                  p { font-size: 20px }
                </code>
              </pre>

              <h4 class="resaltado my-5 w-75">Referenciando con el atributo id</h4>
              <p class="pathway-extreme-p text-justify mt-4">El atributo id es como un nombre que identifica al elemento. Esto significa que el valor de este atributo no puede ser duplicado. Este nombre debe ser único en todo el documento.</p>

              <pre class="pre-content-list bg-blue_2 my-5">
                <code>
                  #texto1 { font-size: 20px }
                </code>
              </pre>

              <h4 class="resaltado my-5 w-75">Referenciando con el atributo class</h4>
              <p class="pathway-extreme-p text-justify mt-4">La mayoría del tiempo, en lugar de utilizar el atributo id para propósitos de estilos es mejor utilizar class. Este atributo es más flexible y puede ser asignado a cada elemento HTML en el documento que comparte un diseño similar:</p>

              <pre class="pre-content-list bg-blue_2 my-5">
                <code>
                  .texto1 { font-size: 20px }
                </code>
              </pre>

              <p class="pathway-extreme-p text-justify mt-4">Para trabajar con el atributo class, debemos declarar la regla CSS con un punto antes del nombre. La ventaja de este método es que insertar el atributo class con el valor texto1 será suficiente para asignar estos estilos a cualquier elemento que queramos</p>

              <p class="pathway-extreme-p text-justify mt-4">La razón por la que debemos utilizar un punto delante del nombre de la regla es que es posible construir referencias más complejas. Por ejemplo, se puede utilizar el mismo valor para el atributo class en diferentes elementos pero asignar diferentes estilos para cada tipo:</p>

              <pre class="pre-content-list bg-blue_2 my-5">
                <code>
                  p.texto1 { font-size: 20px }
                </code>
              </pre>

              <h4 class="resaltado my-5 w-75">Referenciando con cualquier atributo</h4>

              <p class="h5 my-5">Selector de Atributo</p>
              <p class="pathway-extreme-p text-justify mt-4">Ahora podemos referenciar un elemento no solo por los
                atributos id y class sino también a través de cualquier otro atributo:</p>

              <pre class="pre-content-list bg-blue_2 my-5">
                <code>
                  p[name] { font-size: 20px }
                </code>
              </pre>

              <p class="pathway-extreme-p text-justify mt-4">Podemos también especificar el valor del atributo:</p>
              <pre class="pre-content-list bg-blue_2 my-5">
                <code>
                  p[name = "mitexto"] { font-size: 20px }
                </code>
              </pre>

              <p class="pathway-extreme-p text-justify mt-4">CSS3 permite combinar "=" con otros para hacer una selección más específica:</p>
              <pre class="pre-content-list bg-blue_2 my-5">
                <code>
                  p[name^="mi"] { font-size: 20px }
                  p[name$="mi"] { font-size: 20px }
                  p[name*="mi"] { font-size: 20px }
                </code>
              </pre>

              <ul>
                <li><i class="bi bi-check2-square text-primary me-1"></i>La regla con el selector ^= será asignada a todo elemento &lt;p&gt; que contiene un atributo name con un valor comenzado en "mi"" (por ejemplo, "mitexto", "micasa”).</li>
                <li><i class="bi bi-check2-square text-primary me-1"></i>La regla con el selector $= será asignada a todo elemento &lt;p&gt; que contiene un atributo name con un valor finalizado en "mi" (por ejemplo "textomi", "casami”).</li>
                <li><i class="bi bi-check2-square text-primary me-1"></i>La regla con el selector *= será asignada a todo elemento &lt;p&gt; que contiene un atributo name con un valor que incluye el texto "mi" (en este caso, el texto podría también encontrarse en el medio, como en "textomicasa")</li>
              </ul>

              <h4 class="resaltado my-5 w-75">Referenciando con pseudo clases</h4>
              <p class="pathway-extreme-p text-justify mt-4">La pseudo clase es agregada usando dos puntos luego de la referencia y antes de su nombre.</p>

              <p class="h5 my-5">Pseudoclase <span class="text-lila_2">nth-child()</span></p>
              <p class="pathway-extreme-p text-justify mt-4">La pseudoclase <span class="text-lila_2">nth-child()</span> nos permite encontrar un hijo específico.</p>

              <pre class="pre-content-list bg-blue_2 my-5">
                <code>
                  p:<span class="text-lila_2">nth-child(2)</span>{
                    &nbsp;&nbsp;&nbsp;background: #999;
                  }

                  <span class="comentario">También:</span>

                  .miclase:<span class="text-lila_2">nth-child(2)</span>{
                    &nbsp;&nbsp;&nbsp;background: #999;
                  }

                  <span class="comentario">Encontrar elementos hijos con posición impar</span>
                  p:<span class="text-lila_2">nth-child(<span class="text-tomato">odd</span>)</span>{
                    &nbsp;&nbsp;&nbsp;background: #999999;
                  }
                  
                  <span class="comentario">Encontrar elementos hijos con posición par</span>
                  p:<span class="text-lila_2">nth-child(<span class="text-tomato">even</span>)</span>{
                    &nbsp;&nbsp;&nbsp;background: #CCCCCC;
                  }
                </code>
              </pre>

              <p class="pathway-extreme-p text-justify mt-4">Existen otras importantes pseudo clases relacionadas con esta última:</p>

              <ul>
                <li><i class="bi bi-check2-square text-warning me-1"></i>first-child: Busca el 1er hijo</li>
                <li><i class="bi bi-check2-square text-warning me-1"></i>last-child: Busca el último hijo</li>
                <li><i class="bi bi-check2-square text-warning me-1"></i>only-child: Afecta un elemento siempre y cuando sea el único hijo disponible.</li>
              </ul>

              <p class="h5">Pseudoclase :not()</p>
              <pre class="pre-content-list bg-blue_2 my-5">
                <code>
                  :not(p){
                    &nbsp;&nbsp;&nbsp;margin: 0px;
                  }    
                </code>
              </pre>
              <p class="pathway-extreme-p text-justify mt-4">La regla anterior asignará un margen de 0 pixeles a cada elemento del documento excepto a los elementos <em>p</em></p>
            </div>

            <!-- Nuevos selectores -->
            <div class="w-100">
              <h3 id="" class="mt-5">Nuevos selectores</h3>
              <p class="pathway-extreme-p text-justify mt-4">Estos selectores usan los símbolos >, + y ~
                para especificar la relación entre elementos.</p>

              <p class="pathway-extreme-p text-justify mt-4">El selector > indica que el elemento a ser afectado por la regla es el elemento de la derecha cuando tiene al de la izquierda como su padre.</p>

              <pre class="pre-content-list bg-blue_2 my-5">
                <code>
                  div > p.mitexto2{
                  &nbsp;&nbsp;&nbsp;color: #990000;
                  }
                </code>
              </pre>

              <p class="pathway-extreme-p text-justify mt-4">En el siguiente ejemplo el selector + afecta al elemento <em>&lt;p&gt;</em> que se encuentra ubicado luego de otro elemento <em>&lt;p&gt;</em> identificado con el valor mitexto2 en su atributo class.</p>

              <pre class="pre-content-list bg-blue_2 my-5">
                <code>
                  p.mitexto2 + p{
                  &nbsp;&nbsp;&nbsp;color: #990000;
                  }
                </code>
              </pre>

              <p class="pathway-extreme-p text-justify mt-4">En el siguiente ejemplo el selector ~ se aplica a todos los elementos <em>&lt;p&gt;</em> que son hermanos y se encuentran luego del elemento <em>&lt;p&gt;</em> identificado con el valor mitexto2 en su atributo class.</p>

              <pre class="pre-content-list bg-blue_2 my-5">
                <code>
                  p.mitexto2 ~ p{
                  &nbsp;&nbsp;&nbsp;color: #990000;
                  }
                </code>
              </pre>

