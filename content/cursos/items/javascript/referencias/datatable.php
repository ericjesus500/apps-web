        <section class="section">
          <h1 class="my-5"><span class="bg-info disabled px-3 rounded">INSTALACIÓN</span></h1>
          <p class="destacado my-5">DataTables es una potente biblioteca de Javascript para añadir funciones de interacción a tablas HTML.</p>
          
          <!-- ¿Qué se necesita? -->
          <article class="px-4 my-5 article">
            <h2 id="" class="text-lila mt-5"><em>¿Qué se necesita?</em></h2>
            <p class="pathway-extreme-p text-justify mt-4">Dar los primeros pasos y poner en funcionamiento DataTables en tu sitio web es bastante sencillo, ya que solo necesitas incluir dos archivos adicionales en tu página:</p>          
          <ul class="my-5">
            <li class="my-2"><i class="bi bi-check-all px-2 text-orange"></i>El archivo Javascript de DataTables.</li>
            <li class="my-2"><i class="bi bi-check-all px-2 text-orange"></i>El archivo CSS de DataTables.</li>
          </ul>          
          <h3 id="dt-requisitos" class="my-5">Requisitos</h3>
          <p class="pathway-extreme-p text-justify mt-4">DataTables requiere jQuery 1.8 o posterior, con compatibilidad total con 3.x y 4.x. Para proyectos nuevos, utilice la última versión disponible.</p>          
          <h4 class="resaltado my-5">HTML</h4>
          <p class="pathway-extreme-p text-justify mt-4">Si genera su documento HTML con un programa del lado del servidor, como un script PHP, un script Ruby, un programa C# o cualquier otro, solo necesita mostrar su tabla como se muestra a continuación.</p>
          <p class="pathway-extreme-p text-justify mt-4">Tenga en cuenta que DataTables puede generar automáticamente el thead y tbody, junto con todas las filas y celdas de la tabla, si utiliza datos de origen Ajax.</p>
          <p class="pathway-extreme-p text-justify mt-4">Tenga en cuenta también que, si bien DataTables admite colspan y rowspan en el encabezado y pie de página de la tabla, no son compatibles con la tabla tbody y no deben estar presentes.</p>
          <pre class="pre-content-list bg-blue_2 my-5">
            <code>
              &lt;table id="myTable" class="display"&gt;
                &nbsp;&nbsp;&nbsp;&lt;thead&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;tr&gt;
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;th&gt;Column 1&lt;/th&gt;
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;th&gt;Column 2&lt;/th&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;/tr&gt;
                &nbsp;&nbsp;&nbsp;&lt;/thead&gt;
                &nbsp;&nbsp;&nbsp;&lt;tbody&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;tr&gt;
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;td&gt;Row 1 Data 1&lt;/td&gt;
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;td&gt;Row 1 Data 2&lt;/td&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;/tr&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;tr&gt;
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;td&gt;Row 2 Data 1&lt;/td&gt;
                        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;td&gt;Row 2 Data 2&lt;/td&gt;
                    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&lt;/tr&gt;
                &nbsp;&nbsp;&nbsp;&lt;/tbody&gt;
              &lt;/table&gt;
            </code>
          </pre>
          
          <h3 id="" class="my-5">Instalación de Javascript/CSS</h3>
          <p class="pathway-extreme-p text-justify mt-4">Los archivos necesarios se pueden instalar de diferentes maneras:</p>
          <ul class="my-5">
            <li class="my-2"><i class="bi bi-check-all px-2 text-orange"></i>Uso de la CDN de DataTables.</li>
            <li class="my-2"><i class="bi bi-check-all px-2 text-orange"></i>Localmente.</li>
            <li class="my-2"><i class="bi bi-check-all px-2 text-orange"></i>Con un gestor de paquetes como NPM o Bower.</li>
          </ul>          
          <h4 class="resaltado my-5">Uso de CDN</h4>
          <p class="pathway-extreme-p text-justify mt-4">El <a class="btn-link" href="https://cdn.datatables.net/" target="_blank">CDN de DataTables</a> funciona con CloudFlare , que tiene servidores perimetrales en todo el mundo, lo que garantiza que el Javascript y el CSS para DataTables puedan cargarse lo más rápido posible en los navegadores de sus visitantes.</p>          
          <p class="pathway-extreme-p text-justify mt-4">Para incluir DataTables en su página, simplemente incluya el siguiente HTML (recuerde incluir jQuery también ):</p>
          <pre class="pre-content-list bg-blue_2 my-5">
            <code>
              <span class="comentario">//Ultima versión al 04.08.2025: 2.3.2</span>
              &lt;link rel="stylesheet" href="https://cdn.datatables.net/2.3.2/css/dataTables.dataTables.css" /&gt;
  
              &lt;script src="https://cdn.datatables.net/2.3.2/js/dataTables.js"&gt;&lt;/script&gt;
            </code>
          </pre>
          <p class="pathway-extreme-p text-justify mt-4">Si desea los estilos de Bootstrap 5:</p>
          <pre class="pre-content-list bg-blue_2 my-5">
            <code>
              <span class="comentario">CSS</span>
              https://cdn.datatables.net/2.3.2/css/dataTables.bootstrap5.min.css
              
              <span class="comentario">JS</span>              
              https://cdn.datatables.net/2.3.2/js/dataTables.min.js
              
              https://cdn.datatables.net/2.3.2/js/dataTables.bootstrap5.min.js              
            </code>
          </pre>          
          <h4 class="resaltado my-5">Instalación local</h4>
          <p class="pathway-extreme-p text-justify mt-4">Simplemente <a class="btn-link" href="https://datatables.net/download" target="_blank">usa el generador de descargas</a> para descargar la última versión de DataTables, selecciona el software y el estilo adicionales que desees y descarga el paquete personalizado. Después, descomprímelo y súbelo a tu servidor web. Tendrás un directorio DataTablesdisponible en tu servidor. Después, incluye DataTables en tu página usando el siguiente HTML:.</p>
          <pre class="pre-content-list bg-blue_2 my-5">
            <code>
              &lt;link rel="stylesheet" href="/DataTables/datatables.css" /&gt;
 
              &lt;script src="/DataTables/datatables.js"&gt;&lt;/script&gt;
            </code>
          </pre>          
          <h4 class="resaltado my-5">Node.js / NPM</h4>
          <p class="pathway-extreme-p text-justify mt-4">DataTables y sus extensiones están disponibles como paquetes NPM. El nombre base del paquete es [nombre del paquete] y las extensiones y las opciones de integración de estilos están disponibles como paquetes individuales. Consulte la guía de instalación de NPM para obtener más información sobre los paquetes disponibles.</p>
          <p class="pathway-extreme-p text-justify mt-4">Por ejemplo, se puede utilizar lo siguiente para instalar DataTables con su estilo predeterminado:</p>
          <pre class="pre-content-list bg-blue_2 my-5">
            <code>
              npm install datatables.net-dt
            </code>
          </pre>          
          <h4 class="resaltado my-5">Módulos ES</h4>
          <p class="pathway-extreme-p text-justify mt-4">Existen diferentes maneras de cargar Javascript. Las dos más comunes, utilizadas en Node.js y en los empaquetadores del lado del servidor, son los módulos ES y CommonJS.</p>
          <p class="pathway-extreme-p text-justify mt-4">El paquete NPM DataTables, junto con todas sus extensiones, puede funcionar con módulos ES de forma nativa.</p>
          <p class="pathway-extreme-p text-justify mt-4">Primero instale los paquetes que necesita, según las instrucciones anteriores, luego importe el módulo:</p>
          <pre class="pre-content-list bg-blue_2 my-5">
            <code>
              import DataTable from 'datatables.net-dt';
 
              let table = new DataTable('#myTable', {
                  &nbsp;&nbsp;&nbsp;// config options...
              });
            </code>
          </pre>
          <p class="pathway-extreme-p text-justify mt-4">Para instalar y usar extensiones, simplemente impórtelas y se registrarán automáticamente en DataTables. Por ejemplo, para usar Responsive:</p>
          <pre class="pre-content-list bg-blue_2 my-5">
            <code>
              import DataTable from 'datatables.net-dt';
              import 'datatables.net-responsive-dt';
               
              let table = new DataTable('#myTable', {
                  &nbsp;&nbsp;&nbsp;responsive: true
              });
            </code>
          </pre>
