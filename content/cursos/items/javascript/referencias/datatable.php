            <h2 id="" class="text-lila mt-5"><em>Inicialización de tablas de datos</em></h2>
            
            <h3 id="" class="my-5">Usando JQuery</h3>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                $(document).ready( function () {
                  &nbsp;&nbsp;&nbsp;$('#myTable').DataTable();
                });
              </code>
            </pre>
            
            <p class="pathway-extreme-p text-justify mt-4">Si desea personalizar su DataTable, puede hacerlo especificando parámetros de configuración en un objeto pasado a la DataTable()función. Para obtener información sobre cómo usar estos parámetros de configuración, consulte la </a class="btn-link" href="https://datatables.net/manual/options" target="_blank">página del manual de opciones</a>.</p>
            <h4 class="resaltado my-5">Ejemplos de Opciones de configuración</h4>
            <p class="pathway-extreme-p text-justify mt-4">La configuración de DataTables se realiza pasando las opciones que se desean definir al constructor de DataTables como un objeto. Por ejemplo:</p>
            <p class="pathway-extreme-p h4 my-4">Opción para deshabilitar la paginación de la tabla:</p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                $('#example').DataTable( {
                  &nbsp;&nbsp;&nbsp;paging: false
                } );
              </code>
            </pre>
            <p class="pathway-extreme-p h4 my-4">Opcion para habilitar el desplazamiento en la tabla:</p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                $('#example').DataTable( {
                  &nbsp;&nbsp;&nbsp;scrollY: 400
                } );
              </code>
            </pre>
            <p class="pathway-extreme-p h4 my-4">Combinar varias opciones </p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                $('#example').DataTable( {
                  &nbsp;&nbsp;&nbsp;paging: false,
                  &nbsp;&nbsp;&nbsp;scrollY: 400
                } );
              </code>
            </pre>
            <p class="pathway-extreme-p h4 my-4">Opcion para cambiar idioma:</p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                <span class="comentario">El archivo json está en un CDN</span>
                var table = new DataTable('#myTable', {
                  &nbsp;&nbsp;&nbsp;language: {
                      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;url: '//cdn.datatables.net/plug-ins/2.3.2/i18n/es-ES.json',
                  &nbsp;&nbsp;&nbsp;},
                });
              </code>
              
              <span class="comentario">Con el uso de JQuery</span>
              <span class="comentario">(El archivo json está en local)</span>
              $(document).ready(function() {
               &nbsp;&nbsp;&nbsp;$('#myTable').DataTable({
                  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;language: {
                      &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;url: '/js/es-ES.json',
                  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;},
               &nbsp;&nbsp;&nbsp;});
            });
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">El objeto que se pasa es simplemente un objeto estándar de Javascript y puede tratarse como tal. ¡Agregue tantas opciones como desee!</p>
            
            <h4 class="resaltado my-5">Cambiar el idioma con ES modules</h4>
            <p class="pathway-extreme-p text-justify mt-4">Si está usando módulos ES (por ejemplo, con Vite o un paquete similar), puede usar el <a class="btn-link" href="https://www.npmjs.com/package/datatables.net-plugins" target="_blank">paquete datatables.net-plugins</a> (archivos .mjs) en el que está disponible esta traducción. En tal caso, su código de inicialización podría verse así:</p>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                import DataTable from 'datatables.net';
                import language from 'datatables.net-plugins/i18n/es-ES.mjs';
                 
                var table = new DataTable('#myTable', {
                    &nbsp;&nbsp;&nbsp;language,
                });
              </code>
            </pre>            
            <p class="pathway-extreme-p text-justify mt-4">Para conocer la gama completa de opciones de configuración disponibles para DataTables, consulte la sección de <a class="btn-link" href="https://datatables.net/reference/option" target="_blank">referencia de opciones</a> de este sitio web.</p>
            
            <h3 id="" class="my-5">Inicialización de dataTable sin jQuery</h3>
            <pre class="pre-content-list bg-blue_2 my-5">
              <code>
                let table = new DataTable('#myTable', {
                  &nbsp;&nbsp;&nbsp;// options
                });
              </code>
            </pre>
            <p class="pathway-extreme-p text-justify mt-4">Donde los parámetros para new DataTable()son:</p>
            <ul class="my-5">
              <li class="my-2"><i class="bi bi-check-all px-2 text-orange"></i>Un selector DOMString o elementos HTML para seleccionar la(s) tabla(s) del DOM.</li>
              <li class="my-2"><i class="bi bi-check-all px-2 text-orange"></i>Un objeto de opciones de DataTables.</li>
            </ul>
            
            
            
            
