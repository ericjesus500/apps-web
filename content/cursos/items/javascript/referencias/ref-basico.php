          <article class="px-4 my-5 article">
            <h2 id="" class="text-lila mt-5"><em>¿Qué es Javascript?</em></h2>            
            <p class="destacado my-5">JavaScript es un lenguaje de programación interpretado  que permite crear contenido web dinámico e interactivo, mejorando la experiencia del usuario con elementos como formularios interactivos, animaciones y actualizaciones de contenido.</p>
            <h3 class="my-5">¿Cómo incluir javascript en un proyecto</h3>
            <p class="pathway-extreme-p text-justify mt-4">Para incluirlo en un proyecto, se usan etiquetas &lt;script&gt;</p>
            <ul class="my-5">
              <li class="fs-5 my-2"><i class="bi bi-check-all px-2 text-orange"></i>Pueden ir dentro del &lt;head&gt; usando el atributo <span class="text-skyblue_2">defer</span>                
              <pre class="my-4 language-html">
                <code class="language-html" data-prismjs-copy="Copiar">
                  &lt;head&gt;
                    &lt;script defer&gt;
                      <span>//Aquí código javascript</span>
                    &lt;/script&gt;
                  &lt;/head&gt;
                </code>
              </pre>
              <pre class="my-4 language-html">
                <code class="language-html" data-prismjs-copy="Copiar">
                  &lt;head&gt;
                    <span>//Enlazando con un archivo externo</span>
                    &lt;script defer src="ruta/a/mi/script"&gt;
                      <span>//Aquí código javascript</span>
                    &lt;/script&gt;
                  &lt;/head&gt;
                </code>
              </pre>
              <div class="nota my-5">
                <div>
                  <span class="text-tomato"><i class="bi bi-exclamation-circle-fill"></i>Nota:</span>
                </div>
                <div class="pe-4">
                  <p class="mt-3"><span class="text-amarillo">Defer</span> difiere la carga del documento permitiendo esperar a que se cargue todo el DOM del documento html.</p>
                </div>
              </div>                
              </li>
              <li class="fs-5 my-5"><i class="bi bi-check-all px-2 text-orange"></i>Puede ir al final antes de cerrar el &lt;body&gt; para asegurar que el contenido HTML se cargue primero. 
                <pre class="my-4">
                  <code class="language-html" data-prismjs-copy="Copiar">
                      <span>//Enlazando con un archivo externo</span>
                      &lt;script src="ruta/a/mi/script"&gt;&lt;/script&gt;
                    &lt;/body&gt;
                  </code>
                </pre>    
              .</li>
            </ul>
            
            

